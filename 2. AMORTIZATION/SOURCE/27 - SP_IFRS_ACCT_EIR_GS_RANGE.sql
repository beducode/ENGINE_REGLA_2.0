CREATE OR REPLACE PROCEDURESP_IFRS_ACCT_EIR_GS_RANGE(IN P_RUNID CHARACTER VARYING DEFAULT 'S_00000_0000'::CHARACTER VARYING, IN P_DOWNLOAD_DATE DATE DEFAULT NULL::DATE, IN P_PRC CHARACTER VARYING DEFAULT 'S'::CHARACTER VARYING, IN P_ID1 BIGINT DEFAULT NULL::BIGINT, IN P_ID2 BIGINT DEFAULT NULL::BIGINT)
 LANGUAGE PLPGSQL
AS $$
DECLARE
    ---- DATE
    V_PREVDATE DATE;
    V_CURRDATE DATE;

    ---- QUERY   
    V_STR_QUERY TEXT;

    ---- TABLE LIST       
    V_TABLEINSERT1 VARCHAR(100);
    V_TABLEINSERT2 VARCHAR(100);
    
    ---- CONDITION
    V_RETURNROWS INT;
    V_RETURNROWS2 INT;
    V_TABLEDEST VARCHAR(100);
    V_COLUMNDEST VARCHAR(100);
    V_SPNAME VARCHAR(100);
    V_OPERATION VARCHAR(100);

    ---- RESULT
    V_QUERYS TEXT;

    --- VARIABLE
    V_SP_NAME VARCHAR(100);
    STACK TEXT; 
    FCESIG TEXT;
BEGIN 
    -------- ====== VARIABLE ======
	GET DIAGNOSTICS STACK = PG_CONTEXT;
	FCESIG := substring(STACK from 'function (.*?) line');
	V_SP_NAME := UPPER(LEFT(fcesig::regprocedure::text, POSITION('(' in fcesig::regprocedure::text)-1));

    IF COALESCE(P_PRC, NULL) IS NULL THEN
        P_PRC := 'S';
    END IF;

    IF COALESCE(P_RUNID, NULL) IS NULL THEN
        P_RUNID := 'S_00000_0000';
    END IF;

    IF COALESCE(P_ID1, NULL) IS NULL THEN
        P_ID1 := 0;
    END IF;

    IF COALESCE(P_ID2, NULL) IS NULL THEN
        P_ID2 := 0;
    END IF;

    IF P_PRC = 'S' THEN 
        V_TABLEINSERT1 := 'IFRS_ACCT_EIR_PAYM_GS_' || P_RUNID || '';
        V_TABLEINSERT2 := 'IFRS_ACCT_EIR_PAYM_GS_DATE_' || P_RUNID || '';
    ELSE 
        V_TABLEINSERT1 := 'IFRS_ACCT_EIR_PAYM_GS';
        V_TABLEINSERT2 := 'IFRS_ACCT_EIR_PAYM_GS_DATE';
    END IF;
    
    IF P_DOWNLOAD_DATE IS NULL 
    THEN
        SELECT
            CURRDATE, PREVDATE INTO V_CURRDATE, V_PREVDATE
        FROM
            IFRS_PRC_DATE;
    ELSE        
        V_CURRDATE := P_DOWNLOAD_DATE;
        V_PREVDATE := V_CURRDATE - INTERVAL '1 DAY';
    END IF;
    
    V_RETURNROWS2 := 0;
    -------- ====== VARIABLE ======

    -------- RECORD RUN_ID --------
    CALL SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, PG_BACKEND_PID(), CURRENT_DATE);
    -------- RECORD RUN_ID --------

    -------- ====== PRE SIMULATION TABLE ======
    IF P_PRC = 'S' THEN
        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'DROP TABLE IF EXISTS ' || V_TABLEINSERT1 || ' ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'CREATE TABLE ' || V_TABLEINSERT1 || ' AS SELECT * FROM IFRS_ACCT_EIR_PAYM_GS WHERE 1=0 ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'DROP TABLE IF EXISTS ' || V_TABLEINSERT2 || ' ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'CREATE TABLE ' || V_TABLEINSERT2 || ' AS SELECT * FROM IFRS_ACCT_EIR_PAYM_GS_DATE WHERE 1=0 ';
        EXECUTE (V_STR_QUERY);
    END IF;
    -------- ====== PRE SIMULATION TABLE ======

    -------- ====== BODY ======
    CALL SP_IFRS_LOG_AMORT(V_CURRDATE, 'START', 'SP_IFRS_ACCT_EIR_GS_RANGE', '');

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'TRUNCATE TABLE ' || V_TABLEINSERT1 || '';
    EXECUTE (V_STR_QUERY);

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'INSERT INTO ' || V_TABLEINSERT1 || ' 
        (
            MASTERID 
		    ,N_INT_RATE 
		    ,INTCALCCODE 
		    ,PREV_PMT_DATE 
		    ,PMT_DATE 
		    ,I_DAYS 
		    ,COUNTER 
		    ,N_OSPRN_PREV 
		    ,N_PRN_PAYMENT 
		    ,N_INT_PAYMENT 
		    ,N_OSPRN 
		    ,DISB_PERCENTAGE 
		    ,DISB_AMOUNT 
		    ,N_CALC_MFLAT_INT_AMT 
		    ,M 
		    ,PERIOD 
        ) SELECT DISTINCT 
            A.MASTERID 
		    ,A.N_INT_RATE 
		    ,A.INTCALCCODE 
		    ,A.PREV_PMT_DATE 
		    ,A.PMT_DATE 
		    ,A.I_DAYS 
		    ,A.COUNTER 
		    ,A.N_OSPRN_PREV 
		    ,A.N_PRN_PAYMENT 
		    ,A.N_INT_PAYMENT 
		    ,A.N_OSPRN 
		    ,A.DISB_PERCENTAGE 
		    ,A.DISB_AMOUNT 
		    ,A.N_CALC_MFLAT_INT_AMT 
		    ,A.M 
		    ,A.PERIOD 
        FROM ' || 'IFRS_ACCT_EIR_PAYM' || ' A 
        JOIN ' || 'IFRS_GS_MASTERID' || ' B 
        ON B.MASTERID = A.MASTERID 
        AND B.ID >= ' || P_ID1 || ' 
        AND B.ID <= ' || P_ID2 || ' 
        WHERE A.PREV_PMT_DATE <> A.PMT_DATE ';
    EXECUTE (V_STR_QUERY);
    
    GET DIAGNOSTICS V_RETURNROWS = ROW_COUNT;
    V_RETURNROWS2 := V_RETURNROWS2 + V_RETURNROWS;
    V_RETURNROWS := 0;

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'TRUNCATE TABLE ' || V_TABLEINSERT2 || '';
    EXECUTE (V_STR_QUERY);

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'INSERT INTO ' || V_TABLEINSERT2 || ' 
        (
            MASTERID 
		    ,DTMIN 
		    ,DTMAX 
		    ,CNTMIN 
		    ,CNTMAX 
		    ,PERIOD 
        ) SELECT 
            A.MASTERID 
		    ,A.DTMIN 
		    ,A.DTMAX 
		    ,A.CNTMIN 
		    ,A.CNTMAX 
		    ,A.PERIOD 
        FROM (
            SELECT 
                MASTERID 
			    ,MIN(PMT_DATE) DTMIN 
			    ,MAX(PMT_DATE) DTMAX 
			    ,MIN(COUNTER) CNTMIN 
			    ,MAX(COUNTER) CNTMAX 
			    ,MAX(PERIOD) PERIOD 
            FROM ' || V_TABLEINSERT1 || ' 
            GROUP BY MASTERID 
        ) A ';
    EXECUTE (V_STR_QUERY);

    ---- END
    CALL SP_IFRS_LOG_AMORT(V_CURRDATE, 'END', 'SP_IFRS_ACCT_EIR_GS_RANGE', '');
        
    RAISE NOTICE 'SP_IFRS_ACCT_EIR_GS_RANGE | AFFECTED RECORD : %', V_RETURNROWS2;
    ---------- ====== BODY ======

    -------- ====== LOG ======
    V_TABLEDEST = V_TABLEINSERT1;
    V_COLUMNDEST = '-';
    V_SPNAME = 'SP_IFRS_ACCT_EIR_GS_RANGE';
    V_OPERATION = 'INSERT';
    
    CALL SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SPNAME, V_OPERATION, V_RETURNROWS2, P_RUNID);
    -------- ====== LOG ======

    -------- ====== RESULT ======
    V_QUERYS = 'SELECT * FROM ' || V_TABLEINSERT1 || '';
    CALL SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SPNAME, V_RETURNROWS2, P_RUNID);
    -------- ====== RESULT ======

END;

$$
