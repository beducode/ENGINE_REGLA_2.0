-- DROP TABLE
DROP TABLE IFRS_LIFETIME_OVERRIDE CASCADE CONSTRAINTS;

-- CREATE TABLE
CREATE TABLE IFRS_LIFETIME_OVERRIDE (
    PKID NUMBER(19,0)
        GENERATED BY DEFAULT ON NULL AS IDENTITY
        ( START WITH 1 INCREMENT BY 1 CACHE 20 ),

    LIFETIME_CONFIGURATION VARCHAR2(250) NOT NULL,
    DOWNLOAD_DATE DATE NOT NULL,
    SYSCODE_SEGMENTATION VARCHAR2(50),

    LIFETIME_RATE NUMBER(18,8) NOT NULL,
    LIFETIME_OVERRIDE NUMBER(18,8) NOT NULL,

    CREATEDBY VARCHAR2(50) DEFAULT 'SYSTEM',
    CREATEDDATE TIMESTAMP DEFAULT CURRENT_DATE,
    CREATEDHOST VARCHAR2(50) DEFAULT 'LOCALHOST',

    UPDATEDBY VARCHAR2(50),
    UPDATEDDATE TIMESTAMP,
    UPDATEDHOST VARCHAR2(50)
);
