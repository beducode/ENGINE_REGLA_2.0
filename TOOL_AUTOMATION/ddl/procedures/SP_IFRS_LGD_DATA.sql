CREATE OR REPLACE EDITIONABLE PROCEDURE PSAK413.SP_IFRS_LGD_DATA
 P_RUNID		 IN VARCHAR2 DEFAULT 'S_00000_0000',
 P_DOWNLOAD_DATE IN DATE, 
 P_SYSCODE		 IN VARCHAR2 DEFAULT '0', 
 P_PRC			 IN VARCHAR2 DEFAULT 'X'
) 
AS
	---- DATE   
    V_CURRDATE DATE;
    V_PREVDATE DATE;
    V_MODEL_ID VARCHAR2(8000);
	V_COUNT NUMBER;

    ---- QUERY  
    V_STR_QUERY VARCHAR2(8000);

	---- TABLE LIST 
	V_TAB_OWNER CONSTANT VARCHAR2(30) := 'PSAK413';
    V_TABLEINSERT1 VARCHAR2(100);
    V_TABLEINSERT2 VARCHAR2(100);
    V_TABLESELECT1 VARCHAR2(100);
    V_TABLESELECT2 VARCHAR2(100);
    V_TABLESELECT3 VARCHAR2(100);
    V_TABLESELECT4 VARCHAR2(100);
	V_TABLECONFIG VARCHAR2(100);
	V_TABLE_NAME VARCHAR2(100);

    ---- CONDITION
    V_RETURNROWS NUMBER;
    V_RETURNROWS2 NUMBER;
    V_TABLEDEST VARCHAR2(100);
    V_COLUMNDEST VARCHAR2(100);
    V_SPNAME VARCHAR2(100) := 'SP_IFRS_LGD_DATA';
    V_OPERATION VARCHAR2(100);

    ---- RESULT
    V_QUERYS CLOB;
BEGIN
   
	-- Handle default values for P_DOWNLOAD_DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE
        INTO V_CURRDATE
        FROM PSAK413.IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;

    V_MODEL_ID := NVL(P_SYSCODE, '0');
	V_PREVDATE := LAST_DAY(ADD_MONTHS(V_CURRDATE, - 1));
 	V_TABLECONFIG := 'IFRS_LGD_WORKOUT_RULES_CONFIG ';
	
    IF P_PRC = 'S' THEN 
        --V_TABLEINSERT1 := 'GTMP_LGD_RUNNING_CONFIG_' || P_RUNID || '';
        V_TABLEINSERT1 := 'IFRS_LGD_DATA_' || P_RUNID || '';
        V_TABLESELECT1 := 'IFRS_DEFAULT_' || P_RUNID || ''; 
        V_TABLESELECT2 := 'IFRS_LGD_RULE_DATA_' || P_RUNID || ''; 
    ELSE 
       -- V_TABLEINSERT1 := 'GTMP_LGD_RUNNING_CONFIG';
        V_TABLEINSERT1 := 'IFRS_LGD_DATA';
        V_TABLESELECT1 := 'IFRS_DEFAULT'; 
        V_TABLESELECT2 := 'IFRS_LGD_RULE_DATA' ;
    END IF;

    -------- RECORD RUN_ID --------
    PSAK413.SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SPNAME, P_RUNID, 0, CURRENT_DATE); 
	COMMIT;
    -------- RECORD RUN_ID --------

    -------- ====== PRE SIMULATION TABLE ======
	----TABLE CONFIG
    IF P_PRC = 'S' THEN
		SELECT COUNT(*) INTO V_COUNT
		FROM ALL_TABLES
		WHERE OWNER = V_TAB_OWNER 
		 AND TABLE_NAME = UPPER(V_TABLEINSERT1);
        
		IF V_COUNT = 0 THEN 
		V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'CREATE TABLE ' || V_TAB_OWNER || '.' || V_TABLEINSERT1 || ' AS SELECT * FROM ' || V_TAB_OWNER || '.IFRS_LGD_DATA ild  WHERE 0=1';
        EXECUTE IMMEDIATE (V_STR_QUERY);
		END IF;

        
    END IF;
	COMMIT;


	EXECUTE IMMEDIATE 'TRUNCATE TABLE GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG ' ; 
		 
	     V_STR_QUERY := 'INSERT INTO GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG 
               SELECT * FROM IFRS_LGD_WORKOUT_RULES_CONFIG 
               WHERE  IS_DELETED = 0 AND (  
                 UPPER(TRIM(SYSCODE_WORKOUT)) IN ( 
                   SELECT UPPER(TRIM(REGEXP_SUBSTR(:p1, ''[^;]+'', 1, LEVEL)))
                   FROM DUAL
                   CONNECT BY REGEXP_SUBSTR(:p1, ''[^;]+'', 1, LEVEL) IS NOT NULL
                 )
                 OR :p2 = ''0''
               )'; 
    EXECUTE IMMEDIATE V_STR_QUERY USING P_SYSCODE, P_SYSCODE, P_SYSCODE;
 
    -------- ====== PRE SIMULATION TABLE ======
 
    IF V_MODEL_ID = '0' THEN 
	V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'DELETE FROM ' || V_TAB_OWNER || '.' || V_TABLEINSERT1 ||' A
    WHERE A.DEFAULT_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') ';
    ELSE
	V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'DELETE FROM ' || V_TAB_OWNER || '.' || V_TABLEINSERT1 ||' A
    WHERE A.DEFAULT_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') 
	 AND EXISTS (SELECT * FROM GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  X WHERE A.LGD_SEGMENT_ID = X.SEGMENTATION_ID)'; 
   
    END IF;
     DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));
     
   EXECUTE IMMEDIATE V_STR_QUERY;
   COMMIT;
  
	-- End Clear Session

	-- RESET STATUS WO
	V_STR_QUERY := ''; 
	V_STR_QUERY := 'UPDATE ' || V_TABLEINSERT1 || ' A 
	SET ACCOUNT_STATUS = ''A'' ,
		A.UPDATEDBY = ''SP_IFRS_LGD_DATA'',
		A.UPDATEDDATE = SYSDATE,
		A.WO_DATE = NULL
	WHERE A.WO_DATE >= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') 
	AND A.DEFAULT_DATE <= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') 
	AND EXISTS (SELECT 1 FROM GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  X WHERE A.LGD_SEGMENT_ID = X.SEGMENTATION_ID) '
	;
	 

	 DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));
	
	EXECUTE IMMEDIATE (V_STR_QUERY);
	COMMIT;
 


    DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));

	-- RESET STATUS CLOSED
	V_STR_QUERY := '';
	V_STR_QUERY := 'UPDATE ' || V_TABLEINSERT1 || ' A 
	SET ACCOUNT_STATUS = ''A'' ,
		A.UPDATEDBY = ''SP_IFRS_LGD_DATA'',
		A.UPDATEDDATE = SYSDATE,
		A.CLOSED_DATE = NULL
	WHERE A.CLOSED_DATE >= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') 
	AND A.DEFAULT_DATE <= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') 
	AND EXISTS (SELECT 1 FROM GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  X WHERE A.LGD_SEGMENT_ID = X.SEGMENTATION_ID) '
	;
	EXECUTE IMMEDIATE (V_STR_QUERY);
	COMMIT;

    DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));

	--POPULATE LGD DATA
	V_STR_QUERY := '';
	V_STR_QUERY := V_STR_QUERY || '
	INSERT INTO ' || V_TAB_OWNER || '.' || V_TABLEINSERT1 || ' (
	 LGD_SEGMENT_ID,DEFAULT_DATE , CUSTOMER_NUMBER, FACILITY_NUMBER, MASTERID,
	CURRENCY, ACCOUNT_STATUS, CURE_FLAG, --CURED_DATE, WO_DATE, CLOSED_DATE,
	OS_AT_DEFAULT, EQV_AT_DEFAULT, OS_WO, EQV_WO, EIR_AT_DEFAULT
	 ,CREATEDBY
	 ,CREATEDDATE
	 )
	SELECT B.SEGMENTATION_ID, A.DOWNLOAD_DATE
	 ,A.CUSTOMER_NUMBER
	 ,A.FACILITY_NUMBER, A.MASTERID
	 ,A.CCY_AT_DEFAULT , ''A'', 0--, NULL, NULL, NULL
	 ,A.OS_AT_DEFAULT
	 ,A.EQV_AT_DEFAULT
	,0,0, A.EIR_AT_DEFAULT
	 ,''SP_IFRS_LGD_DATA'' CREATEDBY 
	 ,SYSDATE CREATEDDATE 
	FROM ' || V_TAB_OWNER || '.' || V_TABLESELECT1 || ' A 
	JOIN GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  B ON A.RULE_ID = B.DEFAULT_RULE_ID 
	WHERE A.DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'')
	AND EXISTS (SELECT 1 FROM  ' || V_TAB_OWNER || '.' || V_TABLESELECT2 || ' C WHERE A.MASTERID = C.MASTERID 
	 AND A.DOWNLOAD_DATE = C.DOWNLOAD_DATE AND C.RULE_ID  =B.SEGMENTATION_ID 
FETCH FIRST 1 ROWS ONLY ) ';

 DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));
	EXECUTE IMMEDIATE (V_STR_QUERY);
	COMMIT;



	-- UPDATE STATUS WO
	V_STR_QUERY := 'UPDATE ' || V_TABLEINSERT1 || ' A 
	SET A.ACCOUNT_STATUS = ''W'',
	A.WO_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD''),
	A.UPDATEDBY = ''SP_IFRS_LGD_DATA_4'',
	A.UPDATEDDATE = SYSDATE
	WHERE EXISTS (SELECT 1 FROM GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  X WHERE A.LGD_SEGMENT_ID = X.PKID)
	AND EXISTS (
		SELECT 1 
		FROM IFRS_MASTER_ACCOUNT_WO Y 
		WHERE A.MASTERID = Y.MASTERID 
			AND Y.DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD''))
	AND A.ACCOUNT_STATUS = ''A''
	AND A.DEFAULT_DATE <= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'')
	' ; 

 	DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000)); 

	EXECUTE IMMEDIATE (V_STR_QUERY);
	COMMIT;

	-- UPDATE STATUS CLOSED
	V_STR_QUERY := 'UPDATE ' || V_TABLEINSERT1 || ' A 
	SET A.ACCOUNT_STATUS = ''C'',
	A.CLOSED_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD''),
	A.UPDATEDBY = ''SP_IFRS_LGD_DATA_4'',
	A.UPDATEDDATE = SYSDATE
	WHERE EXISTS (SELECT 1 FROM GTMP_IFRS_LGD_WORKOUT_RULES_CONFIG  X WHERE A.LGD_SEGMENT_ID = X.PKID)
	AND EXISTS (
		SELECT 1 
		FROM IFRS_MASTER_ACCOUNT_CLOSE Y 
		WHERE A.MASTERID = Y.MASTERID 
			AND Y.DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD''))
	AND A.ACCOUNT_STATUS = ''A''
	AND A.DEFAULT_DATE <= TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'')
	' ; 


 DBMS_OUTPUT.PUT_LINE(SUBSTR(V_STR_QUERY, 1, 30000));
	EXECUTE IMMEDIATE (V_STR_QUERY);
	COMMIT;
    -------- ====== BODY ======

    -------- ====== LOG ======
    V_TABLEDEST := V_TABLEINSERT1;
    V_COLUMNDEST := '-';
    V_SPNAME := 'SP_IFRS_LGD_DATA';
    V_OPERATION := 'INSERT';
    
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SPNAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
	COMMIT;
    -------- ====== LOG ======

    -------- ====== RESULT ======
    V_QUERYS := 'SELECT * FROM ' || V_TABLEINSERT2 || ' WHERE DEFAULT_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''', ''YYYY-MM-DD'') ';
    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SPNAME, NVL(V_RETURNROWS2,0), P_RUNID);
	COMMIT;
    -------- ====== RESULT ======

END