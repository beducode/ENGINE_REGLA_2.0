CREATE OR REPLACE EDITIONABLE PROCEDURE PSAK413.SP_IFRS_PATCH_DPD_CIF_NEW
dtcurr DATE;
dtprev DATE;
BEGIN
	
	dtcurr := '31-DEC-2025';
	 
	WHILE  dtcurr >= '31-JAN-2020' -- '31-DEC-2025'
	LOOP 
		dtprev := ADD_MONTHS(dtcurr, -1);

	
EXECUTE IMMEDIATE 'TRUNCATE TABLE X_PSAK_DPD_CIF';

INSERT INTO X_PSAK_DPD_CIF
(
	 DOWNLOAD_DATE
	,SCIF 
	,MAX_DPD 
)
SELECT 
	 DOWNLOAD_DATE 
	,GLOBAL_CUSTOMER_NUMBER
	,max(DAY_PAST_DUE)
FROM IFRS_MASTER_ACCOUNT_MONTHLY imam 
WHERE DOWNLOAD_DATE = dtcurr
AND GROUP_SEGMENT NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND GLOBAL_CUSTOMER_NUMBER IS NOT NULL
GROUP BY DOWNLOAD_DATE 
	,GLOBAL_CUSTOMER_NUMBER;


INSERT INTO X_PSAK_DPD_CIF
(
	 DOWNLOAD_DATE
	,SCIF 
	,max(MAX_DPD )
)
SELECT 
	 DOWNLOAD_DATE 
	,CUSTOMER_NUMBER
	,DAY_PAST_DUE
FROM IFRS_MASTER_ACCOUNT_MONTHLY imam 
WHERE DOWNLOAD_DATE = dtcurr
AND GROUP_SEGMENT NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND GLOBAL_CUSTOMER_NUMBER IS NULL
GROUP BY DOWNLOAD_DATE 
	,CUSTOMER_NUMBER;


MERGE INTO 	IFRS_MASTER_ACCOUNT_MONTHLY a USING
(
	SELECT 
		 imam.DOWNLOAD_DATE
		,imam.MASTERID 
		,dpdcif.MAX_DPD 
	FROM IFRS_MASTER_ACCOUNT_MONTHLY imam, 
		X_PSAK_DPD_CIF dpdcif
	WHERE 
	IMAM.CUSTOMER_NUMBER = DPD_CIF.SCIF
	and
	IMAM.DOWNLOAD_DATE = dpdcif.DOWNLOAD_DATE
	and
	IMAM.DOWNLOAD_DATE = dtcurr
) dat 
ON 
(
	a.DOWNLOAD_DATE = dat.DOWNLOAD_DATE 
	AND 
	A.MASTERID = dat.MASTERID
)
WHEN MATCHED THEN UPDATE 
		SET DPD_CIF = dat.MAX_DPD;
		

MERGE INTO 	IFRS_MASTER_ACCOUNT_MONTHLY a USING
(
	SELECT 
		 imam.DOWNLOAD_DATE
		,imam.MASTERID 
		,dpdcif.MAX_DPD 
	FROM IFRS_MASTER_ACCOUNT_MONTHLY imam, 
		X_PSAK_DPD_CIF dpdcif
	WHERE 
	IMAM.GLOBAL_CUSTOMER_NUMBER = DPD_CIF.SCIF
	and
	IMAM.DOWNLOAD_DATE = dpdcif.DOWNLOAD_DATE
	and
	IMAM.DOWNLOAD_DATE = dtcurr
) dat 
ON 
(
	a.DOWNLOAD_DATE = dat.DOWNLOAD_DATE 
	AND 
	A.MASTERID = dat.MASTERID
)
WHEN MATCHED THEN UPDATE 
		SET DPD_CIF = dat.MAX_DPD;


		
	dbms_output.put_line(dtcurr||'|'||dtprev);
	dtcurr := last_day(TO_DATE(dtcurr)-1-TO_NUMBER(TO_CHAR(TO_DATE(dtcurr),'DD')));

END LOOP;

END