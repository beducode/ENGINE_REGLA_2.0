	WHILE  dtcurr <=   '31-DEC-2025'
	LOOP 
		
		SP_IFRS_LGD_RULE_DATA('S_00000_0000', dtcurr, 'WPC-20260211-000000006;WPC-20260211-000000014', 'M');
		
		SP_IFRS_LGD_DATA('S_00000_0000', dtcurr, 'WPC-20260211-000000006;WPC-20260211-000000014', 'M');
		
