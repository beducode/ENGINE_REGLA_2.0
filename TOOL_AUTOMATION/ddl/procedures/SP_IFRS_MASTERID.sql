CREATE OR REPLACE EDITIONABLE PROCEDURE PSAK413.SP_IFRS_MASTERID
	V_CURRDATE DATE;
	V_PREVDATE DATE;
	
BEGIN
       

	 SELECT 
	 	 CURRDATE
	 	,PREVDATE 
	 	INTO 
	 	 V_CURRDATE
	 	,V_PREVDATE
	 FROM IFRS_PRC_DATE_AMORT;
     

	INSERT INTO IFRS_MASTER_ID 
	(
		MASTER_ACCOUNT_CODE
	   ,ACCOUNT_NUMBER
	   ,IAS_CLASS
	   ,EFFDATE
	   ,CREATED_DATE
	)
	SELECT
		MASTER_ACCOUNT_ID,
		ACCOUNT_NUMBER,
		'A' AS IAS_CLASS,
		V_CURRDATE,
		SYSDATE
	FROM IFRS_STG_MASTER_ACCOUNT isma 
	WHERE 
	DOWNLOAD_DATE = V_CURRDATE
	AND
	NOT EXISTS (SELECT * FROM IFRS_MASTER_ID B WHERE isma.MASTER_ACCOUNT_ID = B.MASTER_ACCOUNT_CODE)
	ORDER BY isma.MASTER_ACCOUNT_ID ASC;
	

     
 END