UPDATE   IFRS_MASTER_ACCOUNT_MONTHLY A  SET DPD_CIF  = (SELECT MAX_DPD FROM X_PSAK_DPD_CIF_1 Y WHERE a.GROUP_SEGMENT = Y.GROUP_SEGMENT AND nvl(a.GLOBAL_CUSTOMER_NUMBER, a.customer_number) = Y.scif   )
 WHERE A.DOWNLOAD_DATE  = dtcurr
AND A.ACCOUNT_STATUS  = 'A'
AND A.GROUP_SEGMENT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND EXISTS (SELECT 1 FROM X_PSAK_DPD_CIF_1 x 
	WHERE A.DOWNLOAD_DATE  = X.DOWNLOAD_DATE AND  a.GROUP_SEGMENT = x.GROUP_SEGMENT AND nvl(a.GLOBAL_CUSTOMER_NUMBER, a.customer_number) = x.scif AND a.DPD_CIF  <> x.max_dpd )	
;

 
COMMIT ;
 
UPDATE X_IFRS_PRCDATE SET CURRDATE = dtcurr ; 
COMMIT ; 
		
