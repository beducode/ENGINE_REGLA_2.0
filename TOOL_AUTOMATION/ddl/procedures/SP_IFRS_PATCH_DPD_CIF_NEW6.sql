SELECT DOWNLOAD_DATE , NVL(GLOBAL_CUSTOMER_NUMBER,CUSTOMER_NUMBER), DAY_PAST_DUE , GROUP_SEGMENT 
FROM PSAK413.IFRS_MASTER_ACCOUNT_MONTHLY 
WHERE DOWNLOAD_DATE  = dtcurr
AND ACCOUNT_STATUS  = 'A'
AND  GROUP_SEGMENT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')

;
 
EXECUTE IMMEDIATE 'TRUNCATE TABLE X_PSAK_DPD_CIF_1';

INSERT INTO X_PSAK_DPD_CIF_1
(
	 DOWNLOAD_DATE
	,SCIF 
	,MAX_DPD 
	,GROUP_SEGMENT
)
SELECT 
	 DOWNLOAD_DATE 
	,SCIF
	,MAX(DPD)
	,GROUP_SEGMENT
FROM XGTMP_IMADPDMAS imam 
WHERE DOWNLOAD_DATE = dtcurr
AND  GROUP_SEGMENT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
GROUP BY 
 DOWNLOAD_DATE 
,SCIF
,GROUP_SEGMENT;

COMMIT ;
 

