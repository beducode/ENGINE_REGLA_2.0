CREATE OR REPLACE EDITIONABLE PROCEDURE "PSAK413"."NITIP_ADAM"
IS
BEGIN
	
	
INSERT INTO IFRS_MASTER_ACCOUNT_MONTHLY
(
	 DOWNLOAD_DATE
	,MASTERID
	,MASTER_ACCOUNT_CODE
	,DATA_SOURCE
	,PRODUCT_TYPE 
	,PRODUCT_CODE 
	,BRANCH_CODE 
	,ACCOUNT_STATUS 
	,ACCOUNT_STATUS_ORI
	,ACCOUNT_NUMBER 
	,CUSTOMER_NUMBER 
	,CUSTOMER_NAME 
	,CURRENCY 
	,EXCHANGE_RATE 
	,OUTSTANDING 
	,PLAFOND 
	,MARGIN_RATE 
	,EIR 
	,FAIR_VALUE_AMOUNT 
	,DAY_PAST_DUE 
	,DPD_CIF 
	,BI_COLLECTABILITY 
	,BI_COLLECT_CIF 
	,STAFF_LOAN_FLAG 
	,COLL_AMOUNT 
	,UNAMORT_FEE_AMT 
	,UNAMORT_COST_AMT 
	,MARGIN_TYPE 
	,DEFAULT_DATE 
	,LOAN_START_DATE 
	,LOAN_DUE_DATE  
	,DIVISION_CODE 
)
SELECT 
	 PMAM.DOWNLOAD_DATE
	,IMI.MASTERID
	,PMAM.MASTER_ACCOUNT_ID
	,PMAM.DATA_SOURCE
	,PMAM.PRODUCT_TYPE 
	,PMAM.PRODUCT_CODE 
	,PMAM.BRANCH_CODE 
	,CASE WHEN NVL(PMAM.ACCOUNT_STATUS,'A') = 'E' THEN 'W' ELSE 'A' END ACCOUNT_STATUS  
	,PMAM.ACCOUNT_STATUS AS ACCOUNT_STATUS_ORI
	,PMAM.ACCOUNT_NUMBER 
	,PMAM.CUSTOMER_NUMBER 
	,PMAM.CUSTOMER_NAME 
	,PMAM.CURRENCY 
	,PMAM.EXCHANGE_RATE 
	,PMAM.OUTSTANDING 
	,PMAM.PLAFOND 
	,PMAM.INTEREST_RATE 
	,PMAM.EIR 
	,PMAM.FAIR_VALUE_AMOUNT 
	,PMAM.DAY_PAST_DUE 
	,PMAM.DAY_PAST_DUE_CIF  
	,PMAM.BI_COLLECTABILITY 
	,PMAM.BI_COLLECTABILITY  
	,CASE WHEN NVL(PMAM.STAFF_LOAN_FLAG ,'N') = 'N' THEN 0 ELSE 1 END AS STAFF_LOAN_FLAG 
	,PMAM.COLLATERAL_VALUE_FOR_IMPAIR 
	,PMAM.UNAMOR_ORIGINATION_FEE_AMT 
	,PMAM.UNAMOR_TRANS_COST_AMT  
	,PMAM.AMORT_TYPE  
	,pmam.DEFAULT_DATE 
	,pmam.LOAN_START_DATE 
	,pmam.LOAN_END_DATE 
	,pmam.DIVISION_CODE 
FROM PSAK_MASTER_ACCOUNT_MONTHLY pmam 
JOIN
IFRS_MASTER_ID IMI
ON
PMAM.MASTER_ACCOUNT_ID = IMI.MASTER_ACCOUNT_CODE; 



INSERT INTO IFRS_MASTER_ACCOUNT_MONTHLY_EXT 
(
	 DOWNLOAD_DATE 
	,MASTERID 
)
SELECT 
	 DOWNLOAD_DATE 
	,MASTERID 
FROM PSAK_MASTER_ACCOUNT_MONTHLY pmam 
JOIN
IFRS_MASTER_ID IMI
ON
PMAM.MASTER_ACCOUNT_ID = IMI.MASTER_ACCOUNT_CODE; 


INSERT INTO IFRS_MASTER_ACCOUNT_MONTHLY
(
	 DOWNLOAD_DATE
	,MASTERID
	,MASTER_ACCOUNT_CODE
	,DATA_SOURCE
	,PRODUCT_TYPE 
	,PRODUCT_CODE 
	,BRANCH_CODE 
	,ACCOUNT_STATUS 
	,ACCOUNT_NUMBER 
	,CUSTOMER_NUMBER 
	,CUSTOMER_NAME 
	,CURRENCY 
	,EXCHANGE_RATE 
	,OUTSTANDING 
	,PLAFOND 
	,MARGIN_RATE 
	,EIR 
	,FAIR_VALUE_AMOUNT 
	,DAY_PAST_DUE 
	,DPD_CIF 
	,BI_COLLECTABILITY 
	,BI_COLLECT_CIF 
	,STAFF_LOAN_FLAG 
	,COLL_AMOUNT 
	,UNAMORT_FEE_AMT 
	,UNAMORT_COST_AMT 
	,MARGIN_TYPE 
	,DEFAULT_DATE 
	,LOAN_START_DATE 
	,LOAN_DUE_DATE 
	,DIVISION_CODE 
	,RESTRUCTURE_FLAG 
	,OUTSTANDING_PAST_DUE 
	,MARGIN_PAST_DUE 
)
SELECT 
	 pmam.FICMISDATE AS DOWNLOAD_DATE
	,IMI.MASTERID
	,'HC_'|| pmam.NOMORREKENING AS MASTER_ACCOUNT_CODE
	,'HC' AS DATA_SOURCE
	,'QARDH' AS PRODUCT_TYPE 
	,pmam.LOANTYPECODE AS PRODUCT_CODE 
	,pmam.KODECABANG AS BRANCH_CODE 
	,'A' AS ACCOUNT_STATUS 
	,pmam.NOMORREKENING  AS ACCOUNT_NUMBER 
	,pmam.NOMORNASABAH AS CUSTOMER_NUMBER 
	,pmam.NAMANASABAH  AS CUSTOMER_NAME 
	,pmam.JENISVALUTA AS CURRENCY 
	,1 AS EXCHANGE_RATE 
	,pmam.JUMLAHBULANLAPORAN  AS OUTSTANDING 
	,NULL AS PLAFOND 
	,NULL AS MARGIN_RATE 
	,NULL AS EIR 
	,NULL AS FAIR_VALUE_AMOUNT 
	,pmam.TUNGGAKANHARI AS DAY_PAST_DUE 
	,NULL AS DPD_CIF 
	,pmam.KUALITAS AS BI_COLLECTABILITY 
	,pmam.KUALITAS AS BI_COLLECT_CIF 
	,NULL AS STAFF_LOAN_FLAG 
	,NULL AS COLL_AMOUNT 
	,NULL AS UNAMORT_FEE_AMT 
	,NULL AS UNAMORT_COST_AMT 
	,NULL AS MARGIN_TYPE 
	,NULL AS DEFAULT_DATE 
	,pmam.JANGKAWAKTUMULAI AS LOAN_START_DATE 
	,pmam.JANGKAWAKTUJATUHTEMPO AS LOAN_DUE_DATE 
	,pmam.DIVISIPISAH AS DIVISION_CODE 
	,CASE WHEN pmam.SIFATPIUTANG IN ('1', '5') THEN 1 ELSE 0 END RESTRUCTURE_FLAG
	,pmam.TUNGGAKANPOKOK AS OUTSTANDING_PAST_DUE
	,pmam.TUNGGAKANMARGIN AS MARGIN_PAST_DUE
FROM 
FORM12_SANDIRINCIANPIUTANGQARDH_RPT_TMP pmam 
JOIN
IFRS_MASTER_ID IMI
ON
'HC_'||TRIM(pmam.NOMORREKENING) = IMI.MASTER_ACCOUNT_CODE;

INSERT INTO IFRS_MASTER_ACCOUNT_MONTHLY_EXT 
(
	 DOWNLOAD_DATE 
	,MASTERID 
)
SELECT 
	 pmam.FICMISDATE AS DOWNLOAD_DATE 
	,IMI.MASTERID 
FROM FORM12_SANDIRINCIANPIUTANGQARDH_RPT_TMP pmam 
JOIN
IFRS_MASTER_ID IMI
ON
'HC_'||TRIM(PMAM.NOMORREKENING) = IMI.MASTER_ACCOUNT_CODE; 


