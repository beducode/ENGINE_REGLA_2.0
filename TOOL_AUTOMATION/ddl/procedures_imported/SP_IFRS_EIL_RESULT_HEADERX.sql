CREATE OR REPLACE EDITIONABLE PROCEDURE "PSAK413"."SP_IFRS_EIL_RESULT_HEADERX" (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AS 
    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;

-- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_TABLEPDCONFIG					VARCHAR2(100);
    V_IFRS_EIL_RESULT_HEADER		VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_EIL_RESULT_HEADER';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
    
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;

    V_MODEL_ID := 0;

    -- CHOOSE TABLE NAMES BASED ON MODE
    IF P_PRC = 'S' THEN
        V_IFRS_EIL_RESULT_HEADER		:= 'IFRS_EIL_RESULT_HEADERX_' || P_RUNID;
    	V_TABLEPDCONFIG 				:= 'GTMP_PD_TERM_STRUCTURE_EIL';
    ELSE
    	V_TABLEPDCONFIG 				:= 'GTMP_PD_TERM_STRUCTURE_EIL';
    	V_IFRS_EIL_RESULT_HEADER		:= 'IFRS_EIL_RESULT_HEADERX';
    END IF;

    BEGIN
        SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, SYSTIMESTAMP);
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE('WARNING: SP_IFRS_RUNNING_LOG FAILED: ' || SQLERRM);
    END;
    COMMIT;

    IF P_PRC = 'S' THEN
        
        -- DROP TABLE IF EXISTS (OWNED BY CURRENT SCHEMA)
        SELECT COUNT(*) INTO V_COUNT
        FROM USER_TABLES
        WHERE TABLE_NAME = UPPER(V_IFRS_EIL_RESULT_HEADER);

        IF V_COUNT > 0 THEN
            V_STR_QUERY := 'DROP TABLE ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_HEADER;
            EXECUTE IMMEDIATE V_STR_QUERY;
        END IF;

        V_STR_QUERY := 'CREATE TABLE ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_HEADER || ' AS SELECT * FROM IFRS_EIL_RESULT_HEADER WHERE 1=0';
        EXECUTE IMMEDIATE V_STR_QUERY;
        
    ELSE
    	    
	   	EXECUTE IMMEDIATE 'DELETE FROM ' || V_IFRS_EIL_RESULT_HEADER || ' WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''' ' ;
   
    END IF;
   
	V_STR_QUERY := '
		INSERT INTO IFRS_EIL_RESULT_HEADERX ( 
			DOWNLOAD_DATE, MASTERID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT, 
			DAY_PAST_DUE, BI_COLLECTABILITY, ACCOUNT_NUMBER, ACCOUNT_STATUS,
			CUSTOMER_NUMBER, CUSTOMER_NAME, EXCHANGE_RATE, IMPAIRED_FLAG, OUTSTANDING, BUCKET_ID, REVOLVING_FLAG, PRODUCT_CODE,
			PRODUCT_TYPE,  LOAN_START_DATE, DAY_PAST_DUE_CIF, RESTRUCTURE_FLAG, EIR, LOAN_DUE_DATE, MARGIN_PAST_DUE,
			SEGMENTATION_ID, LT_RULE_ID,   DATA_SOURCE, PRODUCT_GROUP, IS_IMPAIRED,   PD_SEGMENT, LGD_SEGMENT,
			EAD_SEGMENT, BUCKET_GROUP, LIFETIME, STAGE, UNAMORT_COST_AMT, UNAMORT_FEE_AMT, MARGIN_ACCRUED, FAIR_VALUE_AMOUNT, EIL_MODEL_ID,
			EAD_RULE_ID, LGD_RULE_ID, LGD_ME_MODEL_ID, PD_RULE_ID, PD_ME_MODEL_ID, CURRENCY, EIL_AMOUNT,   PD,LGD, EIL_PRINCIPAL,EIL_MARGIN,EAD_AMOUNT,
			CREATEDBY, CREATEDDATE)
		SELECT DOWNLOAD_DATE, MASTERID, MAX(GROUP_SEGMENT), MAX(SEGMENT), MAX(SUB_SEGMENT), MAX(DAY_PAST_DUE), MAX(BI_COLLECTABILITY), MAX(ACCOUNT_NUMBER), MAX(ACCOUNT_STATUS),
			MAX(CUSTOMER_NUMBER), MAX(CUSTOMER_NAME), MAX(EXCHANGE_RATE), MAX(IMPAIRED_FLAG), MAX(OUTSTANDING), MAX(BUCKET_ID), MAX(REVOLVING_FLAG), MAX(PRODUCT_CODE), MAX(PRODUCT_TYPE),  
			MAX(LOAN_START_DATE), MAX(DAY_PAST_DUE_CIF), MAX(RESTRUCTURE_FLAG), MAX(EIR), MAX(LOAN_DUE_DATE), MAX(MARGIN_PAST_DUE), MAX(SEGMENTATION_ID), MAX(LT_RULE_ID),  
			MAX(DATA_SOURCE), MAX(PRODUCT_GROUP), MAX(IS_IMPAIRED), MAX(PD_SEGMENT), MAX(LGD_SEGMENT), MAX(EAD_SEGMENT), MAX(BUCKET_GROUP), MAX(LIFETIME), MAX(STAGE), MAX(UNAMORT_COST_AMT),
			MAX(UNAMORT_FEE_AMT), MAX(MARGIN_ACCRUED), MAX(FAIR_VALUE_AMOUNT), MAX(EIL_MODEL_ID), MAX(EAD_RULE_ID), MAX(LGD_RULE_ID), MAX(LGD_ME_MODEL_ID), MAX(PD_RULE_ID), MAX(PD_ME_MODEL_ID), MAX(CURRENCY),
			SUM(EIL_AMOUNT), AVG(PD),AVG(LGD), SUM(EIL_PRINCIPAL), SUM(EIL_MARGIN) , MAX(CASE WHEN SEQ = 0 THEN EAD_PRINCIPAL + EAD_MARGIN  ELSE 0 END ) EAD,
			''IFRS_EIL_RESULT_HEADER'' AS CREATEDBY,SYSDATE AS CREATEDDATE
			FROM IFRS_EIL_RESULT_DETAILX 
			WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || '''
			GROUP BY DOWNLOAD_DATE, MASTERID' ;
			 
	
		--DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);			
		EXECUTE IMMEDIATE V_STR_QUERY;
		COMMIT;
        -----------------------------
	    -- Log & insert final data
	    -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_HEADER;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
    V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_HEADER ||
                ' WHERE EFF_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')' ;

    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        -- ROLLBACK TO SAFE STATE AND RE-RAISE
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR IN V_IFRS_EIL_RESULT_HEADER: ' || SQLERRM);
        RAISE;
END