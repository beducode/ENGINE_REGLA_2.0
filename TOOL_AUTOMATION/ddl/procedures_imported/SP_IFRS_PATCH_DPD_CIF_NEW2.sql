CREATE OR REPLACE EDITIONABLE PROCEDURE "PSAK413"."SP_IFRS_PATCH_DPD_CIF_NEW2" 
AS
--declare
dtcurr DATE;
dtprev DATE;
BEGIN
	
	dtcurr := '31-DEC-2025';
	 
	WHILE  dtcurr >= '31-JAN-2020' -- '31-DEC-2025'
	LOOP 
		dtprev := ADD_MONTHS(dtcurr, -1);

	
EXECUTE IMMEDIATE 'TRUNCATE TABLE X_PSAK_DPD_CIF';

INSERT INTO X_PSAK_DPD_CIF
(
	 DOWNLOAD_DATE
	,SCIF 
	,MAX_DPD 
)
SELECT 
	 DOWNLOAD_DATE 
	,GLOBAL_CUSTOMER_NUMBER
	,MAX(DAY_PAST_DUE)
FROM IFRS_MASTER_ACCOUNT_MONTHLY imam 
WHERE DOWNLOAD_DATE = dtcurr
AND NVL(GROUP_SEGMENT,'') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND GLOBAL_CUSTOMER_NUMBER IS NOT NULL
AND ACCOUNT_sTATUS = 'A'
GROUP BY 
 DOWNLOAD_DATE 
,GLOBAL_CUSTOMER_NUMBER;

COMMIT ;

INSERT INTO X_PSAK_DPD_CIF
(
	 DOWNLOAD_DATE
	,SCIF 
	,MAX_DPD 
)
SELECT 
	 DOWNLOAD_DATE 
	,CUSTOMER_NUMBER
	,MAX(DAY_PAST_DUE)
FROM IFRS_MASTER_ACCOUNT_MONTHLY imam 
WHERE DOWNLOAD_DATE = dtcurr
AND NVL(GROUP_SEGMENT,'') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND GLOBAL_CUSTOMER_NUMBER IS NULL
AND ACCOUNT_sTATUS  = 'A'
GROUP BY 
 DOWNLOAD_DATE 
,CUSTOMER_NUMBER;

COMMIT ;



UPDATE  IFRS_MASTER_ACCOUNT_MONTHLY A  
SET A.DPD_CIF  = (SELECT MAX_DPD FROM X_PSAK_DPD_CIF X WHERE A.DOWNLOAD_DATE  = X.DOWNLOAD_DATE  AND NVL(A.GLOBAL_CUSTOMER_NUMBER,A.CUSTOMER_NUMBER) = X.SCIF  )
WHERE A.DOWNLOAD_DATE  = dtcurr
AND A.ACCOUNT_STATUS  = 'A'
AND NVL(A.GROUP_SEGMENT,'') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS');

	COMMIT ;
 

		
	dbms_output.put_line(dtcurr||'|'||dtprev);
	dtcurr := last_day(TO_DATE(dtcurr)-1-TO_NUMBER(TO_CHAR(TO_DATE(dtcurr),'DD')));

END LOOP;

END