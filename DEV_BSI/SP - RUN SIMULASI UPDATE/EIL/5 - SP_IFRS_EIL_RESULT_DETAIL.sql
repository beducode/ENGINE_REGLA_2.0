CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_EIL_RESULT_DETAIL (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS 
    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;
    V_EIL_MODEL_ID  NUMBER;

    -- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_IFRS_EIL_RESULT_DETAIL		VARCHAR2(100);
    V_GTMP_PD_TERM_STRUCTURE_EIL    VARCHAR2(100);
    V_IFRS_PD_TERM_STRUCTURE        VARCHAR2(100);
    V_IFRS_EIL_MODEL                VARCHAR2(100);
    V_IFRS_EAD_RESULT               VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_EIL_RESULT_DETAIL';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
    
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;

    V_MODEL_ID := NVL(P_SYSCODE, '0');
   
    EXECUTE IMMEDIATE 'SELECT PKID FROM IFRS_EIL_MODEL_HEADER WHERE SYSCODE_EIL = ''' || P_SYSCODE || ''' OR (''' || P_SYSCODE || ''' = ''0'' AND IS_EOM = 1) AND IS_DELETE = 0 FETCH FIRST 1 ROW ONLY' INTO V_EIL_MODEL_ID;
    COMMIT;
    
    -- CHOOSE TABLE NAMES BASED ON MODE
    IF P_PRC = 'S' THEN
        V_IFRS_EIL_RESULT_DETAIL		:= 'IFRS_EIL_RESULT_DETAIL_' || P_RUNID;
    	V_GTMP_PD_TERM_STRUCTURE_EIL 	:= 'GTMP_PD_TERM_STRUCTURE_EIL_' || P_RUNID;
        V_IFRS_PD_TERM_STRUCTURE        := 'IFRS_PD_TERM_STRUCTURE_' || P_RUNID;
        V_IFRS_EIL_MODEL                := 'IFRS_EIL_MODEL_' || P_RUNID;
        V_IFRS_EAD_RESULT			    := 'IFRS_EAD_RESULT_' || P_RUNID;
    ELSE
    	V_IFRS_EIL_RESULT_DETAIL		:= 'IFRS_EIL_RESULT_DETAIL';
        V_GTMP_PD_TERM_STRUCTURE_EIL 	:= 'GTMP_PD_TERM_STRUCTURE_EIL';
        V_IFRS_PD_TERM_STRUCTURE        := 'IFRS_PD_TERM_STRUCTURE';
        V_IFRS_EIL_MODEL                := 'IFRS_EIL_MODEL';
        V_IFRS_EAD_RESULT			    := 'IFRS_EAD_RESULT';
    END IF;

	-------- RECORD RUN_ID --------
    PSAK413.SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, CURRENT_DATE);
	COMMIT;

    IF P_PRC = 'S' THEN
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_EIL_RESULT_DETAIL', V_IFRS_EIL_RESULT_DETAIL);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_PD_TERM_STRUCTURE_EIL', V_GTMP_PD_TERM_STRUCTURE_EIL);
    END IF;

    EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_PD_TERM_STRUCTURE_EIL;
    COMMIT;

    EXECUTE IMMEDIATE 'DELETE FROM ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_DETAIL || ' WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''' ' ;
    COMMIT;
   
    V_STR_QUERY := '
	INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_PD_TERM_STRUCTURE_EIL || ' (EFF_DATE,SEGMENTATION_ID,PD_RULE_ID,MODEL_ID,FL_SEQ,FL_YEAR,FL_MONTH,BUCKET_ID,PD,PD_DATE,LGD,LGD_DATE)
	SELECT A.EFF_DATE, B.SEGMENTATION_ID ,A.PD_RULE_ID, A.MODEL_ID , A.FL_SEQ , A.FL_YEAR, A.FL_MONTH, A.BUCKET_ID , A.PD,B.PD_DATE, B.LGD, B.LGD_DATE  
	FROM ' || V_TAB_OWNER || '.' || V_IFRS_PD_TERM_STRUCTURE || ' A
    JOIN ' || V_TAB_OWNER || '.' || V_IFRS_EIL_MODEL || ' B ON A.EFF_DATE = B.PD_DATE AND A.PD_RULE_ID = B.PD_RULE_ID AND A.MODEL_ID = B.PD_ME_MODEL_ID 
    WHERE A.PD > 0 AND B.DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''' ';
	
    DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);
    EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;
	
	V_STR_QUERY := '
		INSERT INTO ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_DETAIL || ' ( DOWNLOAD_DATE, MASTERID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT, RATING_CODE, DAY_PAST_DUE, BI_COLLECTABILITY,
			 ACCOUNT_NUMBER, ACCOUNT_STATUS, CUSTOMER_NUMBER, CUSTOMER_NAME, EXCHANGE_RATE, IMPAIRED_FLAG, OUTSTANDING, BUCKET_ID, REVOLVING_FLAG, PRODUCT_CODE,
			 PRODUCT_TYPE, PLAFOND, LOAN_START_DATE, DAY_PAST_DUE_CIF, RESTRUCTURE_FLAG, EIR, LOAN_DUE_DATE, MARGIN_PAST_DUE, SEGMENTATION_ID, LT_RULE_ID, CUSTOMER_GRADE,
			 PRODUCT_ENTITY, DATA_SOURCE, PRODUCT_GROUP, IS_IMPAIRED, IFRS9_CLASS, PD_SEGMENT, LGD_SEGMENT, EAD_SEGMENT, BUCKET_GROUP, LIFETIME, STAGE, UNAMORT_COST_AMT,
			 UNAMORT_FEE_AMT, MARGIN_ACCRUED, FAIR_VALUE_AMOUNT, EAD_BALANCE, EAD_MARGIN, EIL_MODEL_ID, EAD_RULE_ID, LGD_RULE_ID, LGD_ME_MODEL_ID,
			 PD_RULE_ID, PD_ME_MODEL_ID, CURRENCY, EIL_AMOUNT, EIL_PRINCIPAL, EIL_MARGIN, FLAG_VAS, UNUSED_AMOUNT, EAD_TYPE, SEQ, EAD_PRINCIPAL,FL_YEAR,FL_MONTH,PD,LGD, CREATEDBY, CREATEDDATE)
		SELECT X.DOWNLOAD_DATE, X.MASTERID, X.GROUP_SEGMENT, X.SEGMENT, X.SUB_SEGMENT, X.RATING_CODE, X.DAY_PAST_DUE, X.BI_COLLECTABILITY,
			 X.ACCOUNT_NUMBER, X.ACCOUNT_STATUS, X.CUSTOMER_NUMBER, X.CUSTOMER_NAME, X.EXCHANGE_RATE, X.IMPAIRED_FLAG, X.OUTSTANDING, X.BUCKET_ID, X.REVOLVING_FLAG, X.PRODUCT_CODE,
			 X.PRODUCT_TYPE, X.PLAFOND, X.LOAN_START_DATE, X.DAY_PAST_DUE_CIF, X.RESTRUCTURE_FLAG, X.EIR, X.LOAN_DUE_DATE, X.MARGIN_PAST_DUE, X.SEGMENTATION_ID, X.LT_RULE_ID, X.CUSTOMER_GRADE,
			 X.PRODUCT_ENTITY, X.DATA_SOURCE, X.PRODUCT_GROUP, X.IS_IMPAIRED, X.IFRS9_CLASS, X.PD_SEGMENT, X.LGD_SEGMENT, X.EAD_SEGMENT, X.BUCKET_GROUP, X.LIFETIME, X.STAGE, X.UNAMORT_COST_AMT,
			 X.UNAMORT_FEE_AMT, X.MARGIN_ACCRUED, X.FAIR_VALUE_AMOUNT, X.EAD_BALANCE, X.EAD_MARGIN, X.EIL_MODEL_ID, X.EAD_RULE_ID, X.LGD_RULE_ID, X.LGD_ME_MODEL_ID,
			 X.PD_RULE_ID, X.PD_ME_MODEL_ID, X.CURRENCY,
			 ((X.EAD_PRINCIPAL + X.EAD_MARGIN) * Y.PD * Y.LGD ) AS EIL_AMOUNT, 
			 ((X.EAD_PRINCIPAL ) * Y.PD * Y.LGD ) AS EIL_PRINCIPAL, 
			 ((X.EAD_MARGIN) * Y.PD * Y.LGD ) AS EIL_MARGIN, 
			 X.FLAG_VAS, X.UNUSED_AMOUNT, X.EAD_TYPE, X.SEQ, X.EAD_PRINCIPAL,Y.FL_YEAR,Y.FL_MONTH,Y.PD,Y.LGD, ''SP_IFRS_EIL_RESULT_DETAIL'', SYSDATE AS CREATEDDATE
		FROM ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT || ' X
			INNER JOIN ' || V_TAB_OWNER || '.' || V_GTMP_PD_TERM_STRUCTURE_EIL || ' Y
			ON  X.SEGMENTATION_ID = Y.SEGMENTATION_ID
			AND X.BUCKET_ID = Y.BUCKET_ID
			AND X.SEQ = Y.FL_SEQ 
		WHERE X.DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''' ';
	
    DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);			
    EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;

    -----------------------------
    -- LOG & INSERT FINAL DATA
    -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_DETAIL;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
	V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_DETAIL;

    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;