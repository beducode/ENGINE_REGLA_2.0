CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_EAD_RESULT (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS 
    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;

	-- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_IFRS_EAD_RESULT				VARCHAR2(100);
	V_GTMP_IFRS_PAYM_SCHD_EAD		VARCHAR2(100);
	V_IFRS_EIL_IMA					VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_EAD_RESULT';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
    
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;

    V_MODEL_ID := NVL(P_SYSCODE, '0');

    -- CHOOSE TABLE NAMES BASED ON MODE
    IF P_PRC = 'S' THEN
        V_IFRS_EAD_RESULT			:= 'IFRS_EAD_RESULT_' || P_RUNID;
    	V_GTMP_IFRS_PAYM_SCHD_EAD 	:= 'GTMP_IFRS_PAYM_SCHD_EAD_' || P_RUNID;
		V_IFRS_EIL_IMA				:= 'IFRS_EIL_IMA_' || P_RUNID;
    ELSE
    	V_IFRS_EAD_RESULT			:= 'IFRS_EAD_RESULT';
		V_GTMP_IFRS_PAYM_SCHD_EAD 	:= 'GTMP_IFRS_PAYM_SCHD_EAD';
		V_IFRS_EIL_IMA				:= 'IFRS_EIL_IMA';
    END IF;


    IF P_PRC = 'S' THEN
		PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_EAD_RESULT', V_IFRS_EAD_RESULT);
		PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_IFRS_PAYM_SCHD_EAD', V_GTMP_IFRS_PAYM_SCHD_EAD);
    END IF;

	-------- RECORD RUN_ID --------
    PSAK413.SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, CURRENT_DATE);
	COMMIT;

	EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT || ' ' ;
	COMMIT;

    EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_IFRS_PAYM_SCHD_EAD || ' ' ;
    COMMIT;	
	
	V_STR_QUERY := 'INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_IFRS_PAYM_SCHD_EAD || ' (DOWNLOAD_DATE, MASTERID, PMTDATE, SEQ, PRINCIPAL, SUM_PRINCIPAL)
		WITH CTE_INIT AS ( 
			SELECT MAX(DOWNLOAD_DATE) AS DOWNLOAD_DATE, MASTERID 
			FROM ' || V_TAB_OWNER || '.IFRS_INITIAL_PAYM_SCHD 
			WHERE DOWNLOAD_DATE <=  TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') 
			GROUP BY MASTERID 
		), CTE AS ( 
		SELECT A.DOWNLOAD_DATE , A.MASTERID ,LAST_DAY(A.PMTDATE) AS PMTDATE ,
		MAX(MONTHS_BETWEEN(LAST_DAY(A.PMTDATE),A.DOWNLOAD_DATE)) AS SEQ ,  
		SUM(A.PRINCIPAL) AS PRINCIPAL 
		FROM ' || V_TAB_OWNER || '.IFRS_PAYM_SCHD_ALL A
		WHERE DOWNLOAD_DATE  <=  TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') 
				AND PMTDATE >  TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') 
				AND EXISTS(SELECT 1 FROM CTE_INIT X WHERE X.DOWNLOAD_DATE = A.DOWNLOAD_DATE AND X.MASTERID = A.MASTERID)
		GROUP BY LAST_DAY(A.PMTDATE),A.DOWNLOAD_DATE , A.MASTERID )
		SELECT DOWNLOAD_DATE, MASTERID, PMTDATE, SEQ, PRINCIPAL, sum(PRINCIPAL) OVER (PARTITION BY MASTERID ORDER BY seq )
		FROM CTE ' ; 
	
	EXECUTE IMMEDIATE V_STR_QUERY;
	COMMIT;

	V_STR_QUERY := 'INSERT INTO ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT || '(DOWNLOAD_DATE, MASTERID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT, RATING_CODE, DAY_PAST_DUE, BI_COLLECTABILITY,
			ACCOUNT_NUMBER, ACCOUNT_STATUS, CUSTOMER_NUMBER, CUSTOMER_NAME, EXCHANGE_RATE, IMPAIRED_FLAG, OUTSTANDING, BUCKET_ID, REVOLVING_FLAG, 
			PRODUCT_CODE, PRODUCT_TYPE, PLAFOND, LOAN_START_DATE, DAY_PAST_DUE_CIF, RESTRUCTURE_FLAG, EIR, LOAN_DUE_DATE, MARGIN_PAST_DUE, SEGMENTATION_ID,
			LT_RULE_ID, CUSTOMER_GRADE, PRODUCT_ENTITY, DATA_SOURCE, PRODUCT_GROUP, IS_IMPAIRED, IFRS9_CLASS, PD_SEGMENT, LGD_SEGMENT, EAD_SEGMENT, BUCKET_GROUP,
			LIFETIME, STAGE, UNAMORT_COST_AMT, UNAMORT_FEE_AMT, MARGIN_ACCRUED, FAIR_VALUE_AMOUNT, EAD_BALANCE, EAD_MARGIN, EIL_MODEL_ID, EAD_RULE_ID, LGD_RULE_ID,
			LGD_ME_MODEL_ID, PD_RULE_ID, PD_ME_MODEL_ID, CURRENCY, EIL_AMOUNT, FLAG_VAS, UNUSED_AMOUNT, EAD_TYPE, SEQ, EAD_PRINCIPAL, CREATEDBY, CREATEDDATE)
			WITH CTE2 (
			DOWNLOAD_DATE,INCLUDE_BULLET_PAYMENT, MASTERID, LIFETIME, SEQ,EAD_BALANCE, EAD_PRINCIPAL, EAD_MARGIN, SUM_PRINCIPAL
			) AS (
			SELECT 
				A.DOWNLOAD_DATE,CASE WHEN A.LOAN_DUE_DATE < A.DOWNLOAD_DATE THEN 0 ELSE 1 END AS INCLUDE_BULLET_PAYMENT, A.MASTERID, A.LIFETIME, 0 AS SEQ, A.EAD_BALANCE,
				A.EAD_BALANCE + A.EAD_BALANCE_ADDITIONAL AS EAD_PRINCIPAL, A.EAD_MARGIN, CAST (0 AS NUMBER(32,6)) SUM_PRINCIPAL
			FROM ' || V_TAB_OWNER || '.' || V_IFRS_EIL_IMA || ' A 
			UNION ALL
			SELECT
				X.DOWNLOAD_DATE, X.INCLUDE_BULLET_PAYMENT, X.MASTERID, X.LIFETIME, X.SEQ + 1 AS SEQ,X.EAD_BALANCE,
				CASE 
					WHEN X.SEQ + 1 = X.LIFETIME AND B.MASTERID IS NULL AND INCLUDE_BULLET_PAYMENT = 1 THEN 0
					ELSE X.EAD_BALANCE - NVL(B.SUM_PRINCIPAL, X.SUM_PRINCIPAL)
				END AS EAD_PRINCIPAL,
				X.EAD_MARGIN,NVL(B.SUM_PRINCIPAL,X.SUM_PRINCIPAL)
			FROM CTE2 X
			LEFT JOIN ' || V_TAB_OWNER || '.' || V_GTMP_IFRS_PAYM_SCHD_EAD || ' B
				ON X.MASTERID = B.MASTERID       
				AND X.SEQ + 1 = B.SEQ
			WHERE X.SEQ + 1 <= X.LIFETIME
		)
		SELECT B.DOWNLOAD_DATE, B.MASTERID, B.GROUP_SEGMENT, B.SEGMENT, B.SUB_SEGMENT, B.RATING_CODE, B.DAY_PAST_DUE, B.BI_COLLECTABILITY, B.ACCOUNT_NUMBER,
			B.ACCOUNT_STATUS, B.CUSTOMER_NUMBER, B.CUSTOMER_NAME, B.EXCHANGE_RATE, B.IMPAIRED_FLAG, B.OUTSTANDING, B.BUCKET_ID, B.REVOLVING_FLAG, B.PRODUCT_CODE,
			B.PRODUCT_TYPE, B.PLAFOND, B.LOAN_START_DATE, B.DAY_PAST_DUE_CIF, B.RESTRUCTURE_FLAG, B.EIR, B.LOAN_DUE_DATE, B.MARGIN_PAST_DUE, B.SEGMENTATION_ID,
			B.LT_RULE_ID, B.CUSTOMER_GRADE, B.PRODUCT_ENTITY, B.DATA_SOURCE, B.PRODUCT_GROUP, B.IS_IMPAIRED, B.IFRS9_CLASS, B.PD_SEGMENT, B.LGD_SEGMENT,
			B.EAD_SEGMENT, B.BUCKET_GROUP, B.LIFETIME, B.STAGE, B.UNAMORT_COST_AMT, B.UNAMORT_FEE_AMT, B.MARGIN_ACCRUED, B.FAIR_VALUE_AMOUNT, B.EAD_BALANCE,
			A.EAD_MARGIN, B.EIL_MODEL_ID, B.EAD_RULE_ID, B.LGD_RULE_ID, B.LGD_ME_MODEL_ID, B.PD_RULE_ID, B.PD_ME_MODEL_ID, B.CURRENCY, B.EIL_AMOUNT,
			B.FLAG_VAS, B.UNUSED_AMOUNT, B.EAD_TYPE, A.SEQ, CASE WHEN A.EAD_PRINCIPAL < 0 THEN 0 ELSE A.EAD_PRINCIPAL END AS EAD_PRINCIPAL, ''SP_IFRS_EAD_RESULT'' AS CREATEDBY, SYSDATE AS CREATEDDATE
		FROM CTE2 A 
		INNER JOIN ' || V_TAB_OWNER || '.' || V_IFRS_EIL_IMA || ' B 
			ON A.DOWNLOAD_DATE = B.DOWNLOAD_DATE 
			AND A.MASTERID = B.MASTERID 
			AND A.SEQ <= CASE WHEN B.STAGE = 1 THEN 11 ELSE A.SEQ END';
					
		EXECUTE IMMEDIATE V_STR_QUERY;
		COMMIT;

	-----------------------------
	-- LOG & INSERT FINAL DATA
	-----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
	V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT;

    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;