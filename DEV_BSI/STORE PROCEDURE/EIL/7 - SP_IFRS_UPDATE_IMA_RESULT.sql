CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_UPDATE_IMA_RESULT (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS

    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;

    -- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_IFRS_MASTER_ACCOUNT_MONTHLY	VARCHAR2(100);
    V_IFRS_EIL_RESULT_HEADER		VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_UPDATE_IMA_RESULT';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;

	-- CHOOSE TABLE NAMES BASED ON MODE
	IF P_PRC = 'S' THEN
        V_IFRS_MASTER_ACCOUNT_MONTHLY	:= 'IFRS_MASTER_ACCOUNT_MONTHLY_' || P_RUNID;
		V_IFRS_EIL_RESULT_HEADER		:= 'IFRS_EIL_RESULT_HEADER_' || P_RUNID;
    ELSE
    	V_IFRS_MASTER_ACCOUNT_MONTHLY	:= 'IFRS_MASTER_ACCOUNT_MONTHLY';
		V_IFRS_EIL_RESULT_HEADER		:= 'IFRS_EIL_RESULT_HEADER';
    END IF;

	-------- RECORD RUN_ID --------
    PSAK413.SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, CURRENT_DATE);
	COMMIT;

    ----------------------------------------------------------------
    -- RESET SEGMENT DATA (DYNAMIC UPDATE)
    ----------------------------------------------------------------
    V_STR_QUERY :=
        'UPDATE ' || V_TAB_OWNER || '.' || V_IFRS_MASTER_ACCOUNT_MONTHLY || ' A                
			 SET A.EIL_AMOUNT = 0
			  ,A.BUCKET_ID = 0                
			  ,A.LIFETIME = 0
			  ,A.LT_RULE_ID = 0               
			  ,A.LGD_RULE_ID = 0          
			  ,A.PD_RULE_ID = 0          
			  ,A.PD_ME_MODEL_ID = 0              
			  ,A.EAD_RULE_ID = 0           
			  ,A.EIL_MODEL_ID = 0
			  ,A.EIL_MARGIN = 0
			  ,A.EIL_POKOK = 0
			  ,A.EAD_AMOUNT = 0
			  ,A.MARGIN_ACCRUED = 0
			  ,A.PD_RATE = 0
			  ,A.LGD_RATE = 0               
			 WHERE A.ACCOUNT_STATUS = ''A''                  
			  AND A.IS_IMPAIRED = 1
			  AND A.IMPAIRED_FLAG = ''C''                  
			  AND A.DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') '; 

	DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);		 
	EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;

    V_STR_QUERY := 'MERGE INTO ' || V_TAB_OWNER || '.' || V_IFRS_MASTER_ACCOUNT_MONTHLY || ' T
				USING (
				  SELECT DOWNLOAD_DATE, MASTERID, EIL_AMOUNT, BUCKET_ID, LT_RULE_ID, LIFETIME,
				         LGD_RULE_ID, LGD_SEGMENT, PD_RULE_ID, PD_ME_MODEL_ID, PD_SEGMENT,
				         EAD_RULE_ID, EAD_SEGMENT, EIL_MODEL_ID, EIL_MARGIN, EIL_PRINCIPAL,
				         PD, LGD, EAD_AMOUNT, MARGIN_ACCRUED
				  FROM ' || V_TAB_OWNER || '.' || V_IFRS_EIL_RESULT_HEADER || ' S
				  WHERE S.DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')
				) S
				ON (S.MASTERID = T.MASTERID AND S.DOWNLOAD_DATE = T.DOWNLOAD_DATE)
				WHEN MATCHED THEN UPDATE SET
				   T.EIL_AMOUNT     = S.EIL_AMOUNT,
				   T.BUCKET_ID      = S.BUCKET_ID,
				   T.LT_RULE_ID     = S.LT_RULE_ID,
				   T.LIFETIME       = S.LIFETIME,
				   T.LGD_RULE_ID    = S.LGD_RULE_ID,
				   T.PD_RULE_ID     = S.PD_RULE_ID,
				   T.PD_ME_MODEL_ID = S.PD_ME_MODEL_ID,
				   T.EAD_RULE_ID    = S.EAD_RULE_ID,
				   T.EIL_MODEL_ID   = S.EIL_MODEL_ID,
				   T.EIL_MARGIN     = S.EIL_MARGIN,
				   T.EIL_POKOK      = S.EIL_PRINCIPAL,
				   T.PD_RATE        = S.PD,
				   T.LGD_RATE       = S.LGD,
				   T.EAD_AMOUNT     = S.EAD_AMOUNT,
				   T.MARGIN_ACCRUED = S.MARGIN_ACCRUED';
   
	DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);
	EXECUTE IMMEDIATE V_STR_QUERY;
	COMMIT;


	-----------------------------
    -- LOG & INSERT FINAL DATA
    -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_MASTER_ACCOUNT_MONTHLY;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';

    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
	V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_MASTER_ACCOUNT_MONTHLY || ' WHERE DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')';

    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;