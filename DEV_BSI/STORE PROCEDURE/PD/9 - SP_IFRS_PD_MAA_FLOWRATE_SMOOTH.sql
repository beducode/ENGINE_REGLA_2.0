CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_PD_MAA_FLOWRATE_SMOOTH (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS
    -- DATE VARIABLES
    V_CURRDATE      DATE;
    V_PREVDATE      DATE;
    V_MODEL_ID      VARCHAR2(22);

    -- LOOP & PROCESS VARIABLES
    V_LOOP1         PLS_INTEGER;
    V_LOOP2         PLS_INTEGER;
    V_LOOP3			PLS_INTEGER;
   
   	V_PREDPD1      NUMBER;
	V_PREDPD2      NUMBER;
	V_NEXTFLOWPD   NUMBER;

    V_CHECK         NUMBER(1);
    V_RESULTPD      BINARY_DOUBLE;
    V_FLOWRATE1     BINARY_DOUBLE;
    V_FLOWRATE2     BINARY_DOUBLE;
    V_PREDICTEDPD1  BINARY_DOUBLE;
    V_PREDICTEDPD2  BINARY_DOUBLE;
    V_NEXTFLOWRATEPD BINARY_DOUBLE;

    -- COUNT & DYNAMIC SQL
    V_COUNT         NUMBER;
    V_STR_QUERY     CLOB;
    V_QUERYS		CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT VARCHAR2(30) := 'PSAK413';
    V_GTMP_PD_RUNNING_SMOOTH_CONFIG VARCHAR2(100);
    V_GTMP_LOGIT_ODR VARCHAR2(100);
    V_GTMP_SMOOTHING_BASE VARCHAR2(100);
    V_GTMP_PD_BASE VARCHAR2(100);
    V_GTMP_PD_RESULT VARCHAR2(100);
    V_GTMP_SMOOTH VARCHAR2(100);
    V_IFRS_PD_MAA_FLOWRATE_SMOOTH VARCHAR2(100);
    V_IFRS_PD_MAA_FLOWRATE_SUM VARCHAR2(100);
    V_TABLEPDCONFIG VARCHAR2(100);
    V_IFRS_PD_LINEST_SMOOTHING VARCHAR2(100);

    -- LOGGING / RESULT
    V_RETURNROWS2   NUMBER;
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_OPERATION     VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100);

    -- CURSOR
    TYPE REF_CURSOR IS REF CURSOR;
    C_RULE        REF_CURSOR;

    V_RULE_ID      VARCHAR2(100);
    V_MAXBUCKET    NUMBER;
BEGIN
	-- set procedure name
    V_SP_NAME := 'SP_IFRS_PD_MAA_FLOWRATE_SMOOTH';
    
    -- Set current and previous dates
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := P_DOWNLOAD_DATE;
    END IF;
    V_PREVDATE := LAST_DAY(ADD_MONTHS(V_CURRDATE, -1));
    V_MODEL_ID := '0';

    -- DETERMINE TABLE NAMES
    IF P_PRC = 'S' THEN
        V_GTMP_PD_RUNNING_SMOOTH_CONFIG := 'GTMP_PD_RUNNING_SMOOTH_CONFIG_' || P_RUNID;
        V_GTMP_LOGIT_ODR := 'GTMP_LOGIT_ODR_' || P_RUNID;
        V_GTMP_SMOOTHING_BASE := 'GTMP_SMOOTHING_BASE_' || P_RUNID;
        V_GTMP_PD_BASE := 'GTMP_PD_BASE_' || P_RUNID;
        V_GTMP_PD_RESULT := 'GTMP_PD_RESULT_' || P_RUNID;
        V_GTMP_SMOOTH := 'GTMP_SMOOTH_' || P_RUNID;
        V_IFRS_PD_MAA_FLOWRATE_SMOOTH := 'IFRS_PD_MAA_FLOWRATE_SMOOTH_' || P_RUNID;
        V_IFRS_PD_MAA_FLOWRATE_SUM := 'IFRS_PD_MAA_FLOWRATE_SUM_' || P_RUNID;
    	V_IFRS_PD_LINEST_SMOOTHING := 'IFRS_PD_LINEST_SMOOTHING_' || P_RUNID;
        V_TABLEPDCONFIG := 'IFRS_PD_RULES_CONFIG_' || P_RUNID;
    ELSE
        V_GTMP_PD_RUNNING_SMOOTH_CONFIG := 'GTMP_PD_RUNNING_SMOOTH_CONFIG';
        V_GTMP_LOGIT_ODR := 'GTMP_LOGIT_ODR';
        V_GTMP_SMOOTHING_BASE := 'GTMP_SMOOTHING_BASE';
        V_GTMP_PD_BASE := 'GTMP_PD_BASE';
        V_GTMP_PD_RESULT := 'GTMP_PD_RESULT';
        V_GTMP_SMOOTH := 'GTMP_SMOOTH';
        V_IFRS_PD_MAA_FLOWRATE_SMOOTH := 'IFRS_PD_MAA_FLOWRATE_SMOOTH';
        V_IFRS_PD_MAA_FLOWRATE_SUM := 'IFRS_PD_MAA_FLOWRATE_SUM';
    	V_IFRS_PD_LINEST_SMOOTHING := 'IFRS_PD_LINEST_SMOOTHING';
        V_TABLEPDCONFIG := 'IFRS_PD_RULES_CONFIG';
    END IF;

        -- PRE-SIMULATION TABLES
    ----------------------------------------------------------------
    IF P_PRC = 'S' THEN
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_PD_RUNNING_SMOOTH_CONFIG', V_GTMP_PD_RUNNING_SMOOTH_CONFIG);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_PD_MAA_FLOWRATE_SMOOTH', V_IFRS_PD_MAA_FLOWRATE_SMOOTH);
        -- PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_PD_MAA_FLOWRATE_SUM', V_IFRS_PD_MAA_FLOWRATE_SUM);
    	PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_PD_LINEST_SMOOTHING', V_IFRS_PD_LINEST_SMOOTHING);
        -- PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('IFRS_PD_RULES_CONFIG', V_TABLEPDCONFIG);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_LOGIT_ODR', V_GTMP_LOGIT_ODR);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_SMOOTHING_BASE', V_GTMP_SMOOTHING_BASE);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_PD_BASE', V_GTMP_PD_BASE);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_PD_RESULT', V_GTMP_PD_RESULT);
        PSAK413.SP_IFRS_CREATE_TABLE_SIMULATE('GTMP_SMOOTH', V_GTMP_SMOOTH);
    END IF;
    COMMIT;
      
   	BEGIN
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_PD_RUNNING_SMOOTH_CONFIG ||'';
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_LOGIT_ODR;
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTHING_BASE;
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE;
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT;
        EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTH;
    EXCEPTION WHEN OTHERS THEN NULL;
    END;
   	COMMIT;

    PSAK413.SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, TO_NUMBER(SYS_CONTEXT('USERENV','SESSIONID')), SYSDATE);
    COMMIT;
   

    -----------------------------
    -- UPDATE DEFF FLOWRATE
    -----------------------------
    V_STR_QUERY := 'UPDATE ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SUM || ' A '||
					' SET (A.FLOWRATE) = A.FLOWRATE_ORI '||
						'WHERE EFF_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') '||
						' AND EXISTS ( SELECT 1 FROM ' || V_TABLEPDCONFIG || 
						' Y WHERE A.PD_RULE_ID = Y.PKID AND Y.PD_METHOD = ''MAA'' AND NVL(IS_DELETED,0) = 0 AND (  
                   UPPER(TRIM(Y.SYSCODE_PD)) IN ( 
                   SELECT UPPER(TRIM(REGEXP_SUBSTR(:1, ''[^;]+'', 1, LEVEL)))
                   FROM DUAL
                   CONNECT BY REGEXP_SUBSTR(:2, ''[^;]+'', 1, LEVEL) IS NOT NULL
                 )
                 OR :3 = ''0''
               ))';
    
	DBMS_OUTPUT.PUT_LINE(V_STR_QUERY);
    EXECUTE IMMEDIATE V_STR_QUERY USING P_SYSCODE, P_SYSCODE, P_SYSCODE;
    COMMIT;
   	--------------------------------
    -- INSERT CONFIG DATA INTO GTMP
    --------------------------------
   	
    V_STR_QUERY := '
        INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_PD_RUNNING_SMOOTH_CONFIG || ' (
            PD_RULE_ID, BUCKET_GROUP, HISTORICAL_DATA,
            CURR_DATE, PREV_DATE, CUT_OFF_DATE
        )
        SELECT PKID, BUCKET_GROUP, HISTORICAL_DATA,
            TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD''),
            LAST_DAY(ADD_MONTHS(TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD''), -1 * INCREMENT_PERIOD)),
            START_HISTORICAL_DATE
        FROM ' || V_TAB_OWNER || '.' || V_TABLEPDCONFIG || ' WHERE PD_METHOD = ''MAA'' AND IS_DELETED = 0 AND INCREMENT_PERIOD < 12 AND (  
                 UPPER(TRIM(SYSCODE_PD)) IN ( 
                   SELECT UPPER(TRIM(REGEXP_SUBSTR(:1, ''[^;]+'', 1, LEVEL)))
                   FROM DUAL
                   CONNECT BY REGEXP_SUBSTR(:2, ''[^;]+'', 1, LEVEL) IS NOT NULL
                 )
                 OR :3 = ''0'' 
               )';
    
    EXECUTE IMMEDIATE V_STR_QUERY USING P_SYSCODE, P_SYSCODE, P_SYSCODE;
    COMMIT;

   	----------------------------------------------------------------
    -- DELETE EXISTING ROWS FROM TARGET WHICH MATCH RUNNING_DATE
    ----------------------------------------------------------------
    
    V_STR_QUERY := '
        DELETE FROM ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SMOOTH || 
        ' tgt WHERE tgt.EFF_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')
          AND PD_RULE_ID IN (SELECT PD_RULE_ID FROM ' || V_GTMP_PD_RUNNING_SMOOTH_CONFIG || ')';
         
	  EXECUTE IMMEDIATE V_STR_QUERY;
	  COMMIT;
    -----------------------------
    -- INITIAL SMOOTHING BASE
    -----------------------------
    
    V_STR_QUERY := '
        INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTHING_BASE || ' (EFF_DATE,PD_RULE_ID,BUCKET_GROUP,BUCKET_FROM,BUCKET_TO,FLOWRATE,SMOOTHED_PD,LOGIT_ODR)
        WITH CTE_INITIAL AS (
		  SELECT A.EFF_DATE, A.PD_RULE_ID, A.BUCKET_GROUP, A.BUCKET_FROM, A.BUCKET_TO, A.FLOWRATE,
		  	LAG(A.FLOWRATE,1,0) OVER (PARTITION BY A.EFF_DATE,A.PD_RULE_ID ORDER BY A.BUCKET_FROM) FLOWRATE_PREV
		  FROM ' || V_IFRS_PD_MAA_FLOWRATE_SUM || ' A  
		  JOIN ' || V_GTMP_PD_RUNNING_SMOOTH_CONFIG || ' B ON A.PD_RULE_ID = B.PD_RULE_ID  
		  LEFT JOIN VW_IFRS_MAX_BUCKET C ON A.BUCKET_GROUP = C.BUCKET_GROUP AND A.BUCKET_TO = C.MAX_BUCKET_ID 
		  WHERE A.EFF_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')
		   AND A.BUCKET_TO = C.MAX_BUCKET_ID
		   AND A.BUCKET_FROM > 0  
		),
		CTE_MIN AS (
		  SELECT PD_RULE_ID, MIN(FLOWRATE) MIN_FLOWRATE
		  FROM CTE_INITIAL
		  WHERE FLOWRATE > 0
		  GROUP BY PD_RULE_ID  
		) 
		
		SELECT  A.EFF_DATE, A.PD_RULE_ID, A.BUCKET_GROUP, A.BUCKET_FROM, A.BUCKET_TO, A.FLOWRATE, 
		  CASE WHEN A.FLOWRATE = 0 THEN B.MIN_FLOWRATE ELSE A.FLOWRATE END SMOOTHED_PD, 
		  CASE WHEN (1 - CASE WHEN A.FLOWRATE = 0 THEN B.MIN_FLOWRATE ELSE A.FLOWRATE END) = 0 THEN 0 
		 ELSE
		   LN( CASE WHEN A.FLOWRATE = 0 THEN B.MIN_FLOWRATE ELSE A.FLOWRATE END / (1 - CASE WHEN A.FLOWRATE = 0 THEN B.MIN_FLOWRATE ELSE A.FLOWRATE END) )
		  END AS LOGIT_ODR  
		FROM CTE_INITIAL A
		JOIN CTE_MIN B ON A.PD_RULE_ID  = B.PD_RULE_ID ';
    EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;

    ----------------------------------------------------------------
    -- POPULATE TMP_LOGIT_ODR
    ----------------------------------------------------------------
    BEGIN
    V_STR_QUERY := '
        INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_LOGIT_ODR || ' (PD_RULE_ID, BUCKET_ID, LOGIT_ODR)
        SELECT PD_RULE_ID, BUCKET_ID, LOGIT_ODR FROM (
            SELECT PD_RULE_ID, BUCKET_FROM AS BUCKET_ID,
                   MAX(BUCKET_FROM) OVER (PARTITION BY PD_RULE_ID) MAX_BUCKET,
                   LOGIT_ODR
            FROM ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTHING_BASE || '
        ) A
        WHERE A.BUCKET_ID < A.MAX_BUCKET';

    	EXECUTE IMMEDIATE V_STR_QUERY;
    EXCEPTION WHEN OTHERS THEN
        NULL;
    END;
    COMMIT;
	
    PSAK413.SP_IFRS_PD_LINEST_SMOOTHING(P_RUNID,P_PRC);

    ----------------------------------------------------------------
    -- POPULATE TMP_PD_BASE (ADD PREDICTED LOGIT & PREDICTED PD)
    ----------------------------------------------------------------
    BEGIN
    V_STR_QUERY := '
        INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || ' (EFF_DATE, PD_RULE_ID, BUCKET_GROUP, BUCKET_FROM, BUCKET_TO, FLOWRATE, SMOOTHED_PD, LOGIT_ODR, PREDICTED_LOGIT_ODR, PREDICTED_PD)
        SELECT A.EFF_DATE, A.PD_RULE_ID, A.BUCKET_GROUP, A.BUCKET_FROM, A.BUCKET_TO, A.FLOWRATE, A.SMOOTHED_PD, A.LOGIT_ODR,
               (B.INTERCEPT + B.SLOPE * A.BUCKET_FROM) AS PREDICTED_LOGIT_ODR,
               (1 / (1 + EXP((B.INTERCEPT + B.SLOPE * A.BUCKET_FROM)* -1))) AS PREDICTED_PD
        FROM ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTHING_BASE || ' A
        JOIN ' || V_TAB_OWNER || '.' || V_IFRS_PD_LINEST_SMOOTHING || ' B ON A.PD_RULE_ID = B.PD_RULE_ID';

    	EXECUTE IMMEDIATE V_STR_QUERY;
    EXCEPTION WHEN OTHERS THEN
        NULL;
    END;
    COMMIT;

    ----------------------------------------------------------------
    -- MAIN LOOPS: ITERATE BUCKET_FROM = 1 .. V_MAXBUCKET
    ----------------------------------------------------------------
    V_STR_QUERY := 'SELECT PD_RULE_ID, MAX(BUCKET_FROM) - 1 AS MAXBUCKET
    FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || '
    GROUP BY PD_RULE_ID';

    OPEN C_RULE FOR V_STR_QUERY;

    LOOP
    FETCH C_RULE INTO
        V_RULE_ID,
        V_MAXBUCKET;

    EXIT WHEN C_RULE%NOTFOUND;
		
		V_RESULTPD := NULL;
	    V_CHECK := 1;
		V_LOOP1 := 1;
		
		WHILE V_LOOP1 <= V_MAXBUCKET 
		LOOP
			
			V_LOOP2 := 1;
			
            EXECUTE IMMEDIATE '
			SELECT A.FLOWRATE, A.PREDICTED_PD,
			   	CASE WHEN B.BUCKET_FROM IS NOT NULL THEN DATA_PD.MAXBUCKET  + 1 ELSE A.BUCKET_FROM END,
			   	CASE WHEN B.BUCKET_FROM IS NOT NULL OR A.FLOWRATE = 0 THEN A.PREDICTED_PD ELSE NULL END
		   	FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || ' A
		  		LEFT JOIN ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || ' B ON A.BUCKET_FROM = B.BUCKET_FROM + 1 AND A.FLOWRATE < NVL(B.PD_RESULT, 0) AND A.PD_RULE_ID = B.PD_RULE_ID
		  	WHERE A.BUCKET_FROM = ' || V_LOOP1 || ' AND A.PD_RULE_ID =  DATA_PD.PD_RULE_ID' INTO V_FLOWRATE1, V_PREDPD1, V_LOOP2, V_RESULTPD;
			
			WHILE V_LOOP2 <= V_MAXBUCKET
			LOOP
				
                EXECUTE IMMEDIATE '
				SELECT NVL(FLOWRATE, 0), NVL(LEAD(FLOWRATE, 1, 999) OVER (ORDER BY BUCKET_FROM), 0), PREDICTED_PD
			   	FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || '
			   	WHERE BUCKET_FROM = ' || V_LOOP2 || ' AND PD_RULE_ID = DATA_PD.PD_RULE_ID' INTO V_FLOWRATE2, V_NEXTFLOWPD, V_PREDPD2;
				
				IF V_FLOWRATE2 < V_FLOWRATE1 OR V_FLOWRATE2 > V_NEXTFLOWPD
				THEN
					V_CHECK := 0;
				END IF;
				
				IF V_CHECK = 0
                    THEN
                        V_LOOP2 := V_MAXBUCKET + 1;
                    ELSE
                        V_LOOP2 := V_LOOP2 + 1;
                END IF;
	    
				
			END LOOP;-- END LOOP2
			
		  	IF V_RESULTPD IS NULL AND V_CHECK = 1
                THEN 
                    V_RESULTPD := V_FLOWRATE1;
                ELSE 
                    V_RESULTPD := V_PREDPD1;
		  	END IF;
	
		  	V_STR_QUERY := 'INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || '
		  	SELECT EFF_DATE, PD_RULE_ID, BUCKET_GROUP, ' || V_LOOP1 || ' BUCKET_FROM, BUCKET_TO, ' || V_FLOWRATE1 || ' FLOWRATE, 
		  		SMOOTHED_PD, LOGIT_ODR, PREDICTED_LOGIT_ODR, ' || V_PREDPD1 || ' PREDICTED_PD, ' || V_RESULTPD || ' PD_RESULT
		  	FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || '
		  	WHERE BUCKET_FROM = ' || V_LOOP1 || ' AND PD_RULE_ID= DATA_PD.PD_RULE_ID'; 
	  		
            EXECUTE IMMEDIATE V_STR_QUERY;
		  	COMMIT ; 
	
	  	V_LOOP1 := V_LOOP1 + 1;
	
		END LOOP ;  
		 			
		EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTH || ''; 
			
		V_STR_QUERY := 'INSERT INTO ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTH || '
		SELECT BUCKET_FROM, FLOWRATE, PREDICTED_PD, PD_RESULT,
	  		LAG(PD_RESULT, 1, 0) OVER (PARTITION BY PD_RULE_ID ORDER BY BUCKET_FROM) LAGPD,
	  		LEAD(PD_RESULT, 1, 999) OVER (PARTITION BY PD_RULE_ID ORDER BY BUCKET_FROM) LEADPD, PD_RULE_ID
	 	FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || '
	 	ORDER BY BUCKET_FROM';
      		
        EXECUTE IMMEDIATE V_STR_QUERY;
		COMMIT ; 
		
        EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTH || ' WHERE FLOWRATE <> PD_RESULT AND FLOWRATE > LAGPD AND FLOWRATE < LEADPD' INTO V_LOOP3;
	
		IF V_LOOP3 > 0 
	   	THEN

            V_STR_QUERY := '
			MERGE INTO ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || ' A 
				USING ( SELECT B.BUCKET_FROM, B.PD_RULE_ID, B.LAGPD, B.LEADPD, 
				LEAD(A2.PD_RESULT, 1, 0) OVER ( PARTITION BY A2.PD_RULE_ID ORDER BY A2.BUCKET_FROM ) AS NEXT_PD_RESULT 
				FROM ' || V_TAB_OWNER || '.' || V_GTMP_SMOOTH || ' B 
				INNER JOIN ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || ' A2 ON A2.BUCKET_FROM = B.BUCKET_FROM 
					AND A2.PD_RULE_ID = B.PD_RULE_ID ) X ON ( A.BUCKET_FROM = X.BUCKET_FROM AND A.PD_RULE_ID = X.PD_RULE_ID ) 
					WHEN MATCHED THEN 
								UPDATE SET A.PD_RESULT = CASE WHEN A.FLOWRATE <> A.PD_RESULT 
															AND A.FLOWRATE > X.LAGPD 
															AND A.FLOWRATE < X.LEADPD 
															AND X.NEXT_PD_RESULT > A.FLOWRATE 
															THEN A.FLOWRATE 
															ELSE A.PD_RESULT END';
                EXECUTE IMMEDIATE V_STR_QUERY;
			    COMMIT ; 
	   	ELSE
	    	NULL;
	   	END IF;
		
	
	END LOOP;

    CLOSE C_RULE;
	
    ----------------------------------------------------------------
    -- INSERT FINAL RESULT INTO TARGET TABLE IFRS_PD_MAA_FLOWRATE_SMOOTH
    ----------------------------------------------------------------
    V_STR_QUERY := 'INSERT INTO ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SMOOTH || ' (EFF_DATE, PD_RULE_ID, BUCKET_GROUP, BUCKET_FROM, BUCKET_TO, FLOWRATE, SMOOTHED_PD, LOGIT_ODR,
	  PREDICTED_LOGIT_ODR, PREDICTED_PD, FLOWRATE_SMOOTH_PD, CREATEDBY, CREATEDDATE, CREATEDHOST)
	SELECT EFF_DATE, PD_RULE_ID, BUCKET_GROUP, BUCKET_FROM, BUCKET_TO, FLOWRATE, SMOOTHED_PD, LOGIT_ODR, PREDICTED_LOGIT_ODR, PREDICTED_PD, 
	  PD_RESULT, ''SP_IFRS_PD_MAA_FLOWRATE_SMOOTH'' CREATEDBY, SYSDATE CREATEDDATE, ''LOCALHOST'' CREATEDHOST
	 FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_RESULT || '
	 UNION
	  SELECT EFF_DATE, PD_RULE_ID, BUCKET_GROUP, BUCKET_FROM, BUCKET_TO, FLOWRATE, SMOOTHED_PD, LOGIT_ODR, PREDICTED_LOGIT_ODR, PREDICTED_PD, 
	  FLOWRATE FLOWRATE_SMOOTH_PD, ''SP_IFRS_PD_MAA_FLOWRATE_SMOOTH'' CREATEDBY, SYSDATE CREATEDDATE, ''LOCALHOST'' CREATEDHOST
	 FROM ' || V_TAB_OWNER || '.' || V_GTMP_PD_BASE || '
	 WHERE BUCKET_FROM = BUCKET_TO';

    EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;
   
    V_STR_QUERY := 'MERGE INTO ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SUM || ' A
    USING ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SMOOTH || ' B
        ON (A.EFF_DATE = B.EFF_DATE
        AND A.PD_RULE_ID = B.PD_RULE_ID
        AND A.BUCKET_GROUP = B.BUCKET_GROUP
        AND A.BUCKET_FROM = B.BUCKET_FROM
        AND A.BUCKET_TO = B.BUCKET_TO)
    WHEN MATCHED THEN
    UPDATE SET A.FLOWRATE = B.FLOWRATE_SMOOTH_PD
    WHERE A.EFF_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')';
   
	EXECUTE IMMEDIATE V_STR_QUERY;
    COMMIT;
    -----------------------------
    -- LOG
    -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SMOOTH;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
    V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_PD_MAA_FLOWRATE_SMOOTH;


    -----------------------------
    -- RESULT
    -----------------------------
    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        IF C_RULE%ISOPEN THEN
            CLOSE C_RULE;
        END IF;

        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;