	 ,B.CURR_DATE  
	 ,A.PD_RULE_ID  
	 ,A.BUCKET_GROUP  
	 ,A.PD_UNIQUE_ID  
	 ,A.BUCKET_ID BUCKET_FROM  
	 ,CASE   
	  WHEN A.BUCKET_ID = D.MAX_BUCKET_ID  
	   OR G.PD_UNIQUE_ID IS NOT NULL   
	   OR A.IS_CURR_DEFAULT = 1  
