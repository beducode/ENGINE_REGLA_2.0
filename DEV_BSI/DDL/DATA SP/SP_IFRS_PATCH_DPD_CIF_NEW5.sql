CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_PATCH_DPD_CIF_NEW5
AS
--declare
dtcurr DATE;
dtprev DATE;
BEGIN
	
	dtcurr := '31-DEC-2025';
	 
	WHILE  dtcurr >= '31-JAN-2020' -- '31-DEC-2025'
	LOOP 
		dtprev := ADD_MONTHS(dtcurr, -1);

	
EXECUTE IMMEDIATE 'TRUNCATE TABLE XGTMP_IMADPDMAS'	;
COMMIT ; 
 
INSERT INTO XGTMP_IMADPDMAS 
SELECT DOWNLOAD_DATE , NVL(GLOBAL_CUSTOMER_NUMBER,CUSTOMER_NUMBER), DAY_PAST_DUE , GROUP_SEGMENT 
FROM PSAK413.IFRS_MASTER_ACCOUNT_MONTHLY 
WHERE DOWNLOAD_DATE  = dtcurr
AND ACCOUNT_STATUS  = 'A'
AND  NVL(GROUP_SEGMENT,'X') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')

;

COMMIT ;  
 	


	 
	
EXECUTE IMMEDIATE 'TRUNCATE TABLE X_PSAK_DPD_CIF';
INSERT INTO X_PSAK_DPD_CIF
(
	 DOWNLOAD_DATE
	,SCIF 
	,MAX_DPD 
)
SELECT 
	 DOWNLOAD_DATE 
	,SCIF 
	,MAX(DPD)
FROM XGTMP_IMADPDMAS imam 
WHERE DOWNLOAD_DATE = dtcurr
AND  NVL(GROUP_SEGMENT,'X') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')  
GROUP BY 
 DOWNLOAD_DATE 
,SCIF  ;

COMMIT ;



UPDATE  IFRS_MASTER_ACCOUNT_MONTHLY A  
SET A.DPD_CIF  = (SELECT MAX_DPD FROM X_PSAK_DPD_CIF X WHERE A.DOWNLOAD_DATE  = X.DOWNLOAD_DATE  AND NVL(A.GLOBAL_CUSTOMER_NUMBER,A.CUSTOMER_NUMBER) = X.SCIF  )
WHERE A.DOWNLOAD_DATE  = dtcurr
AND A.ACCOUNT_STATUS  = 'A'
AND NVL(A.GROUP_SEGMENT,'X') NOT IN ('BTB_MRB_EMAS','HASANAH_CARD','GADAI_EMAS')
AND EXISTS (SELECT 1 FROM X_PSAK_DPD_CIF x 
	WHERE A.DOWNLOAD_DATE  = X.DOWNLOAD_DATE AND   nvl(a.GLOBAL_CUSTOMER_NUMBER, a.customer_number) = x.scif AND (a.DPD_CIF  <> x.max_dpd OR A.DPD_CIF  IS NULL ) )	
;

 

	COMMIT ;
 
	
UPDATE PSAK413.X_IFRS_PRCDATE  SET CURRDATE  = DTCURR; COMMIT ;
		
	dbms_output.put_line(dtcurr||'|'||dtprev);
	dtcurr := last_day(TO_DATE(dtcurr)-1-TO_NUMBER(TO_CHAR(TO_DATE(dtcurr),'DD')));

END LOOP;
  

END;
/
