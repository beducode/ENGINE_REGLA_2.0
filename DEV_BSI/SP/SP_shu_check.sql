DECLARE V_SEQ NUMBER ; 
 
BEGIN  
	
	V_SEQ := 0 ;

	WHILE V_SEQ < 30 
	LOOP 
		INSERT INTO IFRS_PD_MMULT_FL (EFF_DATE, PD_RULE_ID, MODEL_ID, MODEL_SEQ, SCENARIO_NO,SEQ, BUCKET_FROM, BUCKET_TO,MMULT)
		SELECT A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ,A.SCENARIO_NO, V_SEQ + 1,  A.BUCKET_FROM , B.BUCKET_TO ,  SUM(A.MMULT * B.NORMDIST_MMULT ) 
		FROM IFRS_PD_MMULT_FL A 
			JOIN IFRS_PD_NORMDIST B ON A.EFF_DATE = B.EFF_DATE AND A.PD_RULE_ID = B.PD_RULE_ID 
				AND A.MODEL_ID = B.MODEL_ID 
				AND A.MODEL_SEQ = B.MODEL_SEQ 
				AND B.BUCKET_FROM = A.BUCKET_TO  
				AND A.SCENARIO_NO = B.SCENARIO_NO
		WHERE A.SEQ = V_SEQ
			AND B.SEQ =    V_SEQ +1 
		GROUP BY A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ, a.BUCKET_FROM, b.BUCKET_TO ,1 ,A.SCENARIO_NO ; 
		
	      V_SEQ := V_SEQ + 1;

		
	END LOOP;	
END ;  



