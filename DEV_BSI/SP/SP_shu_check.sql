INSERT INTO IFRS_PD_NORMDIST (EFF_DATE, PD_RULE_ID , MODEL_ID, MODEL_SEQ, scenario_no,seq, BUCKET_FROM, BUCKET_TO,NORMDIST_MMULT )

WITH 
cte2 as (

selecT a.pd_rule_id,a.model_id, a.model_seq,scenario_no , EXTRACT(YEAR FROM me_period) - 2026 seq, max(a.coeff_val )+ sum(b.coeff_val * c.me_val ) odr
from IFRS_FL_MODEL_DATA a 
	join IFRS_FL_MODEL_DATA b on a.model_id = b.model_id and a.model_seq = b.model_seq 
	join IFRS_MEV_FORECAST C ON B.COEFF_NAme = c.me_code 
where EXTRACT(MONTH FROM ME_PERIOD) = 12
	and a.coeff_name = 'const' and b.coeff_name <> 'const' 
	and nvl(a.is_selected,0) = 1 
 group by a.model_id, a.model_seq, EXTRACT(YEAR FROM me_period) - 2026, scenario_no, a.pd_rule_id
),
CTE AS (
SELECT a.EFF_DATE , a.PD_RULE_ID , b.MODEL_ID, b.MODEL_SEQ  ,b.scenario_no,b.seq,
	a.BUCKET_FROM , a.BUCKET_TO ,FN_NORMDIST(a.NORMINV_MMULT +  b.odr)  NORMDIST_MMULT
FROM IFRS_PD_NORMINV A 
	join cte2 b on a.pd_rule_id = b.pd_rule_id
), CTE3 AS (
SELECT A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ,a.scenario_no, a.seq, A.BUCKET_FROM, A.BUCKET_TO ,  A.NORMDIST_MMULT, 
	LEAD( A.NORMDIST_MMULT,1,0) OVER (PARTITION BY A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ,a.scenario_no, a.seq, A.BUCKET_FROM 
		ORDER BY A.BUCKET_TO) LEADNORM
FROM CTE  A) , 
CTE4 AS (
SELECT A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ,a.scenario_no, a.seq, A.BUCKET_FROM, A.BUCKET_TO ,  
MAX(A.NORMDIST_MMULT) - SUM(NVL(B.NORMDIST_MMULT,0) - NVL(B.LEADNORM,0) )
FROM CTE3  A 
 LEFT JOIN CTE3 B ON A.EFF_DATE = B.EFF_DATE AND A.PD_RULE_ID = B.PD_RULE_ID
		AND A.MODEL_ID = B.MODEL_ID AND A.MODEL_SEQ = B.MODEL_SEQ 
		AND A.BUCKET_FROM = B.BUCKET_FROM 
		AND A.scenario_no = B.scenario_no
		AND A.SEQ = B.SEQ
		AND A.BUCKET_TO < B.BUCKET_TO 
GROUP BY A.EFF_DATE, A.PD_RULE_ID, A.MODEL_ID, A.MODEL_SEQ, A.BUCKET_FROM, A.BUCKET_TO , a.scenario_no, a.seq
) 
