CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_MASTER_ACCOUNT_MONTHLY (
    p_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
	p_DOWNLOAD_DATE IN DATE,
    p_PRC           IN CHAR DEFAULT 'M'
)
AS
    v_RULE_ID        VARCHAR2(250);
    v_TABLE_NAME     VARCHAR2(100);
    v_STR_SQL        CLOB;
    v_GROUP_SEGMENT  VARCHAR2(250);
    v_SEGMENT        VARCHAR2(250);
    v_SUB_SEGMENT    VARCHAR2(250);
    v_CONDITION      VARCHAR2(4000);
    v_CURRDATE       DATE;
    v_EXCEPT_ID      VARCHAR2(50);
    v_ERROR_FLAG     NUMBER(1) := 0;
    v_TABLENAME      VARCHAR2(200);
    v_TABLE_MONTHLY      VARCHAR2(200);
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_COUNT          NUMBER;
   
    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_MASTER_ACCOUNT_MONTHLY';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
BEGIN
    ----------------------------------------------------------------
    -- INIT
    ----------------------------------------------------------------
    IF p_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO v_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        v_CURRDATE := p_DOWNLOAD_DATE;
    END IF;
	
    IF P_PRC = 'S' THEN
        v_TABLENAME  	 := 'IFRS_MASTER_ACCOUNT_' || P_RUNID;
        v_TABLE_MONTHLY  := 'IFRS_MASTER_ACCOUNT_MONTHLY_' || P_RUNID;
    ELSE
        v_TABLENAME  	 := 'IFRS_MASTER_ACCOUNT';
        v_TABLE_MONTHLY  := 'IFRS_MASTER_ACCOUNT_MONTHLY';
    END IF;
	
   
     IF P_PRC = 'S' THEN
        -- DROP TABLE IF EXISTS (OWNED BY CURRENT SCHEMA)
        SELECT COUNT(*) INTO V_COUNT
        FROM USER_TABLES
        WHERE TABLE_NAME = UPPER(v_TABLE_MONTHLY);

        IF V_COUNT > 0 THEN
            v_STR_SQL := 'DROP TABLE ' || V_TAB_OWNER || '.' || v_TABLE_MONTHLY;
            EXECUTE IMMEDIATE v_STR_SQL;
        END IF;

        	v_STR_SQL := 'CREATE TABLE ' || V_TAB_OWNER || '.' || v_TABLE_MONTHLY || ' AS SELECT * FROM IFRS_MASTER_ACCOUNT_MONTHLY WHERE 1=0';
        EXECUTE IMMEDIATE v_STR_SQL;
        
    ELSE
    	    
	   	EXECUTE IMMEDIATE 'DELETE FROM ' || v_TABLE_MONTHLY || ' WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || ''' ' ;
    END IF;
   
    ----------------------------------------------------------------
    -- INSERT IMA_MONTHLY
    ----------------------------------------------------------------
    v_STR_SQL :=
        'INSERT INTO ' || v_TABLE_MONTHLY || ' (DOWNLOAD_DATE, MASTERID, DATA_SOURCE, ACCOUNT_STATUS, ACCOUNT_STATUS_ORI, GLOBAL_CUSTOMER_NUMBER, CUSTOMER_NUMBER, CUSTOMER_NAME, FACILITY_NUMBER,
