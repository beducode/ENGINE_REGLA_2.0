				 CURRENCY, EXCHANGE_RATE, PRODUCT_GROUP, PRODUCT_TYPE, PRODUCT_CODE, PRODUCT_ENTITY, SEGMENTATION_ID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT,
				 GL_CONSTNAME, MARGIN_RATE, MARGIN_TYPE, MARKET_RATE, BELOW_MARKET_FLAG, ORIGINAL_COLLECTABILITY, BI_COLLECTABILITY, BI_COLLECT_CIF, DAY_PAST_DUE,
				 DPD_CIF, DPD_START_DATE, DPD_ZERO_COUNTER, DEFAULT_DATE, DEFAULT_FLAG, WRITEOFF_FLAG, IMPAIRED_FLAG, IS_IMPAIRED, PLAFOND, PLAFOND_CASH, UNUSED_AMOUNT,
				 DOWN_PAYMENT_AMOUNT, INITIAL_OUTSTANDING, OUTSTANDING, OUTSTANDING_IDC, OUTSTANDING_JF, OUTSTANDING_BANK, OUTSTANDING_PAST_DUE, OUTSTANDING_WO,
				 MARGIN_PAST_DUE, MARGIN_ACCRUED, INSTALLMENT_AMOUNT, INSTALLMENT_GRACE_PERIOD, LOAN_START_DATE, LOAN_DUE_DATE, TENOR, REMAINING_TENOR,
				 LOAN_START_AMORTIZATION, LOAN_END_AMORTIZATION, NEXT_PAYMENT_DATE, NEXT_INT_PAYMENT_DATE, LAST_PAYMENT_DATE, FIRST_INSTALLMENT_DATE,
				 PAYMENT_CODE, PAYMENT_TERM, MARGIN_CALCULATION_CODE, MARGIN_PAYMENT_TERM, RESTRUCTURE_DATE, RESTRUCTURE_FLAG, POCI_FLAG, STAFF_LOAN_FLAG,
				 BTB_FLAG, COMMITTED_FLAG, REVOLVING_FLAG, SPPI_RESULT, BM_RESULT, IAS_CLASS, IFRS9_CLASS, AMORT_TYPE, EIR_STATUS, ECF_STATUS, EIR, EIR_AMOUNT,
				 AVG_EIR, FAIR_VALUE_AMOUNT, INITIAL_UNAMORT_COST, INITIAL_UNAMORT_FEE, UNAMORT_COST_AMT, UNAMORT_FEE_AMT, DAILY_AMORT_AMT, UNAMORT_AMT_TOTAL_JF,
				 UNAMORT_FEE_AMT_JF, UNAMORT_COST_AMT_JF, INITIAL_BENEFIT, UNAMORT_BENEFIT, UNAMORT_DISCOUNT_PREMIUM, DISCOUNT_PREMIUM_AMOUNT, STAGE,
				 INITIAL_RATING_CODE, RATING_CODE, EXT_RATING_AGENCY, EXT_RATING_CODE, EXT_INIT_RATING_CODE, RATING_DOWNGRADE,
				 EXT_RATING_DOWNGRADE, BUCKET_GROUP, BUCKET_ID, LIFETIME, EIL_MODEL_ID, EAD_RULE_ID, EAD_AMOUNT, LGD_RULE_ID, PD_RULE_ID, CCF_RULE_ID, PD_ME_MODEL_ID,
				 EIL_POKOK, EIL_MARGIN, EIL_AMOUNT, CA_UNWINDING_AMOUNT, IA_UNWINDING_AMOUNT, IA_UNWINDING_SUM_AMOUNT, BEGINNING_BALANCE, ENDING_BALANCE, WRITEBACK_AMOUNT,
				 CHARGE_AMOUNT, WATCHLIST_FLAG, COLL_AMOUNT, FACILITY_NUMBER_PARENT, EARLY_PAYMENT, EARLY_PAYMENT_FLAG, EARLY_PAYMENT_DATE, DIVISION_CODE, JF_FLAG,
				 CREATEDBY, CREATEDDATE, CREATEDHOST, UPDATEDBY, UPDATEDDATE, UPDATEDHOST, DAY_PAST_DUE_ORI, OUTSTANDING_PREVIOUS, EQV_OUTSTANDING_CIF, UNAMORT_OTHERS,
				 PD_RATE, LGD_RATE, LT_RULE_ID, SICR_FLAG, MULTIJASA_FLAG, RESTRU_BENCANA_FLAG)
		SELECT DOWNLOAD_DATE, MASTERID, DATA_SOURCE, ACCOUNT_STATUS, ACCOUNT_STATUS_ORI, GLOBAL_CUSTOMER_NUMBER, CUSTOMER_NUMBER, CUSTOMER_NAME, FACILITY_NUMBER,
				 ACCOUNT_NUMBER, MASTER_ACCOUNT_CODE, PREVIOUS_ACCOUNT_NUMBER, BRANCH_CODE, BRANCH_CODE_OPEN, LIMIT_CURRENCY,
				 CURRENCY, EXCHANGE_RATE, PRODUCT_GROUP, PRODUCT_TYPE, PRODUCT_CODE, PRODUCT_ENTITY, SEGMENTATION_ID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT,
				 GL_CONSTNAME, MARGIN_RATE, MARGIN_TYPE, MARKET_RATE, BELOW_MARKET_FLAG, ORIGINAL_COLLECTABILITY, BI_COLLECTABILITY, BI_COLLECT_CIF, DAY_PAST_DUE,
				 DPD_CIF, DPD_START_DATE, DPD_ZERO_COUNTER, DEFAULT_DATE, DEFAULT_FLAG, WRITEOFF_FLAG, IMPAIRED_FLAG, IS_IMPAIRED, PLAFOND, PLAFOND_CASH, UNUSED_AMOUNT,
				 DOWN_PAYMENT_AMOUNT, INITIAL_OUTSTANDING, OUTSTANDING, OUTSTANDING_IDC, OUTSTANDING_JF, OUTSTANDING_BANK, OUTSTANDING_PAST_DUE, OUTSTANDING_WO,
				 MARGIN_PAST_DUE, MARGIN_ACCRUED, INSTALLMENT_AMOUNT, INSTALLMENT_GRACE_PERIOD, LOAN_START_DATE, LOAN_DUE_DATE, TENOR, REMAINING_TENOR,
				 LOAN_START_AMORTIZATION, LOAN_END_AMORTIZATION, NEXT_PAYMENT_DATE, NEXT_INT_PAYMENT_DATE, LAST_PAYMENT_DATE, FIRST_INSTALLMENT_DATE,
				 PAYMENT_CODE, PAYMENT_TERM, MARGIN_CALCULATION_CODE, MARGIN_PAYMENT_TERM, RESTRUCTURE_DATE, RESTRUCTURE_FLAG, POCI_FLAG, STAFF_LOAN_FLAG,
				 BTB_FLAG, COMMITTED_FLAG, REVOLVING_FLAG, SPPI_RESULT, BM_RESULT, IAS_CLASS, IFRS9_CLASS, AMORT_TYPE, EIR_STATUS, ECF_STATUS, EIR, EIR_AMOUNT,
				 AVG_EIR, FAIR_VALUE_AMOUNT, INITIAL_UNAMORT_COST, INITIAL_UNAMORT_FEE, UNAMORT_COST_AMT, UNAMORT_FEE_AMT, DAILY_AMORT_AMT, UNAMORT_AMT_TOTAL_JF,
				 UNAMORT_FEE_AMT_JF, UNAMORT_COST_AMT_JF, INITIAL_BENEFIT, UNAMORT_BENEFIT, UNAMORT_DISCOUNT_PREMIUM, DISCOUNT_PREMIUM_AMOUNT, STAGE,
				 INITIAL_RATING_CODE, RATING_CODE, EXT_RATING_AGENCY, EXT_RATING_CODE, EXT_INIT_RATING_CODE, RATING_DOWNGRADE,
				 EXT_RATING_DOWNGRADE, BUCKET_GROUP, BUCKET_ID, LIFETIME, EIL_MODEL_ID, EAD_RULE_ID, EAD_AMOUNT, LGD_RULE_ID, PD_RULE_ID, CCF_RULE_ID, PD_ME_MODEL_ID,
				 EIL_POKOK, EIL_MARGIN, EIL_AMOUNT, CA_UNWINDING_AMOUNT, IA_UNWINDING_AMOUNT, IA_UNWINDING_SUM_AMOUNT, BEGINNING_BALANCE, ENDING_BALANCE, WRITEBACK_AMOUNT,
				 CHARGE_AMOUNT, WATCHLIST_FLAG, COLL_AMOUNT, FACILITY_NUMBER_PARENT, EARLY_PAYMENT, EARLY_PAYMENT_FLAG, EARLY_PAYMENT_DATE, DIVISION_CODE, JF_FLAG,
				 SYSDATE, CREATEDDATE, CREATEDHOST, UPDATEDBY, UPDATEDDATE, UPDATEDHOST, DAY_PAST_DUE_ORI, OUTSTANDING_PREVIOUS, EQV_OUTSTANDING_CIF, UNAMORT_OTHERS,
				 PD_RATE, LGD_RATE, LT_RULE_ID, SICR_FLAG, MULTIJASA_FLAG, RESTRU_BENCANA_FLAG
			FROM ' || v_TABLENAME || ' WHERE DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(v_currdate,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') '; 
			 
	EXECUTE IMMEDIATE v_STR_SQL;
    COMMIT;
   
    -----------------------------
	-- Log & insert final data
	-----------------------------
   
    V_TABLEDEST := V_TAB_OWNER || '.' || v_TABLE_MONTHLY;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(v_currdate, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
    V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || v_TABLE_MONTHLY ||
                ' WHERE DOWNLOAD_DATE = TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'')' ;

    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        -- ROLLBACK TO SAFE STATE AND RE-RAISE
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR IN SP_IFRS_MASTER_ACCOUNT_MONTHLY: ' || SQLERRM);
        RAISE;
END;