CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_PREPARE_EAD_RESULT_DEV (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS 
    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;

    -- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_IFRS_EIL_IMA					VARCHAR2(100);
    V_TABLEPDCONFIG					VARCHAR2(100);
    V_IFRS_EAD_RESULT				VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_PREPARE_EAD_RESULT_DEV';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
    V_SEGMENTATION_ID             NUMBER := 0;
    
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := LAST_DAY(TO_DATE( TO_CHAR(P_DOWNLOAD_DATE,'YYYY-MM-DD') ,'YYYY-MM-DD'));
    END IF;

    --V_MODEL_ID := 0;

    -- CHOOSE TABLE NAMES BASED ON MODE
    IF P_PRC = 'S' THEN
        V_IFRS_EAD_RESULT		:= 'IFRS_EAD_RESULT_' || P_RUNID;
        V_IFRS_EIL_IMA			:= 'IFRS_EIL_IMA_' || P_RUNID;
    	V_TABLEPDCONFIG 		:= 'TEMP_IFRS_PAYM_SCHD_EAD_' || P_RUNID
    ELSE
    	V_TABLEPDCONFIG 		:= 'TEMP_IFRS_PAYM_SCHD_EAD';
    	V_IFRS_EAD_RESULT		:= 'IFRS_EAD_RESULT';
    	V_IFRS_EIL_IMA			:= 'IFRS_EIL_IMA';
    END IF;

    BEGIN
        SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, SYSTIMESTAMP);
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE('WARNING: SP_IFRS_RUNNING_LOG FAILED: ' || SQLERRM);
    END;

   EXECUTE IMMEDIATE 'TRUNCATE TABLE TEMP_IFRS_PAYM_SCHD_EAD' ; 
   COMMIT;
   		
		V_STR_QUERY := ' INSERT INTO TEMP_IFRS_PAYM_SCHD_EAD (DOWNLOAD_DATE, MASTERID, PMTDATE, SEQ, PRINCIPAL, SUM_PRINCIPAL)
			WITH CTE AS ( 
			SELECT A.DOWNLOAD_DATE , A.MASTERID ,LAST_DAY(A.PMTDATE) AS PMTDATE ,
			MAX(MONTHS_BETWEEN(LAST_DAY(A.PMTDATE),TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') )) AS SEQ ,  
			SUM(A.PRINCIPAL) AS PRINCIPAL 
			FROM IFRS_PAYM_SCHD_ALL A 
			WHERE DOWNLOAD_DATE  <=  TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') 
					AND PMTDATE >  TO_DATE(''' || TO_CHAR(V_CURRDATE,'YYYY-MM-DD') || ''',''YYYY-MM-DD'') 
					AND EXISTS(SELECT 1 FROM IFRS_INITIAL_PAYM_SCHD X WHERE X.DOWNLOAD_DATE = A.DOWNLOAD_DATE AND X.MASTERID = A.MASTERID)
			GROUP BY LAST_DAY(A.PMTDATE),A.DOWNLOAD_DATE , A.MASTERID )
			SELECT DOWNLOAD_DATE, MASTERID, PMTDATE, SEQ, PRINCIPAL, sum(PRINCIPAL) OVER (PARTITION BY MASTERID ORDER BY seq )
			FROM CTE ' ; 
		
		EXECUTE IMMEDIATE V_STR_QUERY;
		COMMIT;
	
      -----------------------------
	  -- Log & insert final data
	  -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_TABLEPDCONFIG;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
    ----------------------------------------------------------------
    -- RESULT PREVIEW
    ----------------------------------------------------------------
	V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_TABLEPDCONFIG || ' WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || '''';
    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), V_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;