CREATE OR REPLACE PROCEDURE PSAK413.SP_IFRS_EAD_RESULT_DEV (
    P_RUNID         IN VARCHAR2 DEFAULT 'S_00000_0000',
    P_DOWNLOAD_DATE IN DATE,
    P_SYSCODE       IN VARCHAR2 DEFAULT '0',
    P_PRC           IN VARCHAR2 DEFAULT 'S'
)
AUTHID CURRENT_USER
AS 
    V_CURRDATE       DATE;
    V_DATADATE       VARCHAR2(20);
    V_TABLE_NAME     VARCHAR2(50);
    V_COUNT          NUMBER;
	V_MODEL_ID       NUMBER;

-- DYNAMIC NAMES / QUERY
    V_STR_QUERY      CLOB;

    -- TABLE NAMES
    V_TAB_OWNER     CONSTANT 		VARCHAR2(30) := 'PSAK413';
    V_IFRS_EIL_IMA					VARCHAR2(100);
    V_TABLEPDCONFIG					VARCHAR2(100);
    V_IFRS_EAD_RESULT				VARCHAR2(100);

    -- LOG / RESULT
    V_TABLEDEST     VARCHAR2(100);
    V_COLUMNDEST    VARCHAR2(100);
    V_SP_NAME       VARCHAR2(100) := 'SP_IFRS_EAD_RESULT_DEV';
    V_OPERATION     VARCHAR2(100);
    V_QUERYS        VARCHAR2(8000);
    V_RETURNROWS2   NUMBER;
    
    V_STR_SQL        CLOB;
    V_STR_SQL_RULE   CLOB;
    V_RULE_ID        VARCHAR2(250);
    V_ID             NUMBER := 0;
    V_MAX_ID         NUMBER := 0;
    V_SEQUENCE       VARCHAR2(50);
    V_SEGMENTATION_ID	NUMBER := 0;
    
BEGIN
	-- HANDLE DEFAULT DOWNLOAD DATE
    IF P_DOWNLOAD_DATE IS NULL THEN
        SELECT CURRDATE INTO V_CURRDATE FROM IFRS_PRC_DATE;
    ELSE
        V_CURRDATE := LAST_DAY(TO_DATE( TO_CHAR(P_DOWNLOAD_DATE,'YYYY-MM-DD') ,'YYYY-MM-DD'));
    END IF;

    V_MODEL_ID := 0;

    -- CHOOSE TABLE NAMES BASED ON MODE
    IF P_PRC = 'S' THEN
        V_IFRS_EAD_RESULT		:= 'IFRS_EAD_RESULT_' || P_RUNID;
        V_IFRS_EIL_IMA			:= 'IFRS_EIL_IMA_' || P_RUNID;
    	V_TABLEPDCONFIG 		:= 'TEMP_IFRS_PAYM_SCHD_EAD_' || P_RUNID;
    ELSE
    	V_TABLEPDCONFIG 		:= 'TEMP_IFRS_PAYM_SCHD_EAD';
    	V_IFRS_EAD_RESULT		:= 'IFRS_EAD_RESULT';
    	V_IFRS_EIL_IMA			:= 'IFRS_EIL_IMA';
    END IF;

    BEGIN
        SP_IFRS_RUNNING_LOG(V_CURRDATE, V_SP_NAME, P_RUNID, 0, SYSTIMESTAMP);
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE('WARNING: SP_IFRS_RUNNING_LOG FAILED: ' || SQLERRM);
    END;
    COMMIT;

    IF P_PRC = 'S' THEN
        -- DROP TABLE IF EXISTS (OWNED BY CURRENT SCHEMA)
        SELECT COUNT(*) INTO V_COUNT
        FROM USER_TABLES
        WHERE TABLE_NAME = UPPER(V_IFRS_EAD_RESULT);

        IF V_COUNT > 0 THEN
            V_STR_QUERY := 'DROP TABLE ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT;
            EXECUTE IMMEDIATE V_STR_QUERY;
        END IF;

        V_STR_QUERY := 'CREATE TABLE ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT || ' AS SELECT * FROM IFRS_EAD_RESULT WHERE 1=0';
        EXECUTE IMMEDIATE V_STR_QUERY;
        
    ELSE
    	    
	   	EXECUTE IMMEDIATE 'TRUNCATE TABLE ' || V_IFRS_EAD_RESULT || ' ' ;
   
    END IF;
	
	FOR REC IN (
		SELECT DISTINCT PKID AS V_SEGMENTATION_ID FROM IFRS_SEGMENTATION_MAPPING 
		WHERE IS_DELETED = 0 AND SEGMENT_TYPE = 'PORTFOLIO'
		ORDER BY PKID 
    )
    LOOP
	    
	    INSERT INTO TRAN_MONITORING_EAD_RESULT (DOWNLOAD_DATE, START_PROCESS, PROCNAME, REMARK)
	    VALUES(V_CURRDATE,SYSDATE, CAST(REC.V_SEGMENTATION_ID AS VARCHAR2(20)), 'RUNNING' );
	   
	    COMMIT;
	   
		V_STR_QUERY := 'INSERT INTO ' || V_IFRS_EAD_RESULT || '(DOWNLOAD_DATE, MASTERID, GROUP_SEGMENT, SEGMENT, SUB_SEGMENT, ACCOUNT_NUMBER, CUSTOMER_NUMBER, EXCHANGE_RATE,
					 OUTSTANDING, BUCKET_ID, REVOLVING_FLAG, PLAFOND, EIR, MARGIN_PAST_DUE, SEGMENTATION_ID, LT_RULE_ID, DATA_SOURCE, BUCKET_GROUP, LIFETIME, STAGE, UNAMORT_COST_AMT,
					 UNAMORT_FEE_AMT, MARGIN_ACCRUED, FAIR_VALUE_AMOUNT, EAD_BALANCE, EAD_MARGIN, EIL_MODEL_ID, EAD_RULE_ID, LGD_RULE_ID, LGD_ME_MODEL_ID, 
					 PD_RULE_ID, PD_ME_MODEL_ID, CURRENCY, EIL_AMOUNT, UNUSED_AMOUNT, SEQ, EAD_PRINCIPAL, CREATEDBY, CREATEDDATE,LOAN_START_DATE, LOAN_DUE_DATE,PMTDATE,IMPAIRED_FLAG)
					WITH CTE2 (
					    DOWNLOAD_DATE,INCLUDE_BULLET_PAYMENT, MASTERID, LIFETIME, SEQ,EAD_BALANCE, EAD_PRINCIPAL, EAD_MARGIN, SUM_PRINCIPAL,PMTDATE, SEGMENTATION_ID
					) AS (
					    SELECT 
					        A.DOWNLOAD_DATE,CASE WHEN A.LOAN_DUE_DATE < A.DOWNLOAD_DATE THEN 0 ELSE 1 END AS INCLUDE_BULLET_PAYMENT, A.MASTERID, A.LIFETIME, 0 AS SEQ, A.EAD_BALANCE,
					        A.EAD_BALANCE + A.EAD_BALANCE_ADDITIONAL AS EAD_PRINCIPAL, A.EAD_MARGIN, CAST (0 AS NUMBER(32,6)) SUM_PRINCIPAL,CAST(NULL AS DATE) AS PMTDATE,
							A.SEGMENTATION_ID
					    FROM ' || V_IFRS_EIL_IMA || ' A 
						WHERE A.SEGMENTATION_ID = ' || REC.V_SEGMENTATION_ID || '
					    UNION ALL
					    SELECT
					        X.DOWNLOAD_DATE, X.INCLUDE_BULLET_PAYMENT, X.MASTERID, X.LIFETIME, X.SEQ + 1 AS SEQ,X.EAD_BALANCE,
					        CASE 
					            WHEN X.SEQ + 1 = X.LIFETIME AND B.MASTERID IS NULL AND INCLUDE_BULLET_PAYMENT = 1 THEN 0
					            ELSE X.EAD_BALANCE - NVL(B.SUM_PRINCIPAL, X.SUM_PRINCIPAL)
					        END AS EAD_PRINCIPAL,
					        X.EAD_MARGIN,NVL(B.SUM_PRINCIPAL,X.SUM_PRINCIPAL),B.PMTDATE, X.SEGMENTATION_ID
					    FROM CTE2 X
					    LEFT JOIN TEMP_IFRS_PAYM_SCHD_EAD B
					        ON X.MASTERID = B.MASTERID       
					       AND X.SEQ + 1 = B.SEQ
					    WHERE X.SEQ + 1 < X.LIFETIME
						AND X.SEGMENTATION_ID = ' || REC.V_SEGMENTATION_ID || '
					)
					SELECT B.DOWNLOAD_DATE, B.MASTERID, B.GROUP_SEGMENT, B.SEGMENT, B.SUB_SEGMENT, B.ACCOUNT_NUMBER, B.CUSTOMER_NUMBER, B.EXCHANGE_RATE,
					 B.OUTSTANDING, B.BUCKET_ID, B.REVOLVING_FLAG, B.PLAFOND, B.EIR, B.MARGIN_PAST_DUE, B.SEGMENTATION_ID,
					 B.LT_RULE_ID, B.DATA_SOURCE, B.BUCKET_GROUP, B.LIFETIME, B.STAGE, B.UNAMORT_COST_AMT, B.UNAMORT_FEE_AMT, B.MARGIN_ACCRUED, B.FAIR_VALUE_AMOUNT, B.EAD_BALANCE,
					 A.EAD_MARGIN, B.EIL_MODEL_ID, B.EAD_RULE_ID, B.LGD_RULE_ID, B.LGD_ME_MODEL_ID, B.PD_RULE_ID, B.PD_ME_MODEL_ID, B.CURRENCY, B.EIL_AMOUNT,
					 B.UNUSED_AMOUNT, A.SEQ, CASE WHEN A.EAD_PRINCIPAL < 0 THEN 0 ELSE A.EAD_PRINCIPAL END AS EAD_PRINCIPAL, ''SP_IFRS_EAD_RESULT'' AS CREATEDBY, SYSDATE AS CREATEDDATE,
					 B.LOAN_START_DATE, B.LOAN_DUE_DATE,A.PMTDATE,B.IMPAIRED_FLAG
					FROM CTE2 A 
					INNER JOIN ' || V_IFRS_EIL_IMA || ' B 
						ON A.DOWNLOAD_DATE = B.DOWNLOAD_DATE 
						AND A.MASTERID = B.MASTERID 
						AND A.SEQ <= CASE WHEN B.STAGE = 1 THEN 11 ELSE A.SEQ END 
					WHERE A.SEGMENTATION_ID = ' || REC.V_SEGMENTATION_ID || '';
					
			EXECUTE IMMEDIATE V_STR_QUERY;
		
			UPDATE TRAN_MONITORING_EAD_RESULT 
			SET END_PROCESS = SYSDATE ,
			REMARK = 'FINISH'
			WHERE ID = (SELECT MAX(ID) AS ID_MAX FROM TRAN_MONITORING_EAD_RESULT);
		
			COMMIT;
	END LOOP;
    -----------------------------
    -- Log & insert final data
    -----------------------------
    V_TABLEDEST := V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT;
    V_COLUMNDEST := '-';
    V_OPERATION := 'INSERT';
 
    PSAK413.SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SP_NAME, V_OPERATION, NVL(V_RETURNROWS2,0), P_RUNID);
    COMMIT;  
   
    ----------------------------------------------------------------
    -- RESULT PREVIEW
    ----------------------------------------------------------------
	V_QUERYS := 'SELECT * FROM ' || V_TAB_OWNER || '.' || V_IFRS_EAD_RESULT || ' WHERE DOWNLOAD_DATE = DATE ''' || TO_CHAR(V_CURRDATE, 'YYYY-MM-DD') || '''';
    PSAK413.SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SP_NAME, NVL(V_RETURNROWS2,0), V_RUNID);
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;

        RAISE_APPLICATION_ERROR(-20001,'ERROR IN ' || V_SP_NAME || ' : ' || SQLERRM);
END;