CREATE OR REPLACE PROCEDURE SP_IFRS_ECL_GATHER_TBL_STATS
AS
BEGIN
    DBMS_STATS.UNLOCK_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_DETAIL');
    DBMS_STATS.UNLOCK_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_DETAIL_CALC');
    DBMS_STATS.UNLOCK_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_HEADER');

    DBMS_STATS.GATHER_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_DETAIL',DEGREE=>8);
    DBMS_STATS.GATHER_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_DETAIL_CALC',DEGREE=>8);
    DBMS_STATS.GATHER_TABLE_STATS ( OWNNAME=>'IFRS', TABNAME=>'IFRS_ECL_RESULT_HEADER',DEGREE=>8);
END;