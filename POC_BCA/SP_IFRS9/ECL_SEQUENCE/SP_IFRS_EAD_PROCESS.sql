CREATE OR REPLACE PROCEDURE SP_IFRS_EAD_PROCESS(v_ECLID NUMBER DEFAULT(0), v_DOWNLOADDATE DATE DEFAULT('1-JAN-1900'))
AS
    V_CURRDATE DATE;
    V_EOM DATE;
    V_FOM DATE;
    V_SP_NAME VARCHAR2(50);
    V_EXISTS NUMBER(1) := 0;
BEGIN

