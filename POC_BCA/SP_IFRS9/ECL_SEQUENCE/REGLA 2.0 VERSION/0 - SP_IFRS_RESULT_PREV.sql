CREATE OR REPLACE PROCEDURE IFRS9_BCA.SP_IFRS_RESULT_PREV
(
    P_DOWNLOAD_DATE    IN DATE DEFAULT NULL,
    P_QUERYS           IN CLOB DEFAULT NULL,
    P_SOURCE_OBJECT    IN VARCHAR2 DEFAULT NULL,
    P_AFFECTED_RECORD  IN NUMBER DEFAULT NULL,
    P_RUNID            IN VARCHAR2 DEFAULT 'S_00000_0000'
)
AS
BEGIN
    INSERT INTO IFRS_RESULT_PREVIEW
        (DOWNLOAD_DATE,
         QUERY_RESULT,
         SOURCE_OBJECT,
         AFFECTED_RECORD,
         RUN_ID,
         PROCESS_DATE)
    VALUES
        (P_DOWNLOAD_DATE,
         P_QUERYS,
         P_SOURCE_OBJECT,
         P_AFFECTED_RECORD,
         P_RUNID,
         SYSTIMESTAMP);

    -- Pengganti RAISE NOTICE di Oracle
    DBMS_OUTPUT.PUT_LINE('QUERY RESULT : ' || P_QUERYS);
END SP_IFRS_RESULT_PREV;