    DELETE R_BACKTEST_IN_SAMPLE_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_BACKTEST_OUT_SMPL_ME_DTL_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_BACKTEST_OUT_SAMPLE_DTL_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_BACKTEST_OUT_SAMPLE_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE IFRS_ME_DERIVED_FORCST_VAR_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_FITTED_ODR_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_SCALING_FACTOR_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_PD_PIT_PEN
    WHERE MODEL_ID = V_MODEL_ID;

    DELETE R_PD_CUMULATIVE_PEN
