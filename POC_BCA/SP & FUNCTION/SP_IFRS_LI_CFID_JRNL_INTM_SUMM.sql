CREATE OR REPLACE PROCEDURE SP_IFRS_LI_CFID_JRNL_INTM_SUMM
AS
  V_CURRDATE    DATE;
  V_PREVDATE    DATE;

BEGIN
    /******************************************************************************
    01. DECLARE VARIABLE
    *******************************************************************************/
    SELECT MAX(CURRDATE),MAX(PREVDATE)
    INTO V_CURRDATE, V_PREVDATE
    FROM IFRS_LI_PRC_DATE_AMORT;


    INSERT INTO IFRS_LI_AMORT_LOG(DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
    VALUES(V_CURRDATE,SYSTIMESTAMP,'START','SP_IFRS_LI_CFID_JRNL_INTM_SUMM','');

    COMMIT;
    /******************************************************************************
    02. DELETE
    *******************************************************************************/
    EXECUTE IMMEDIATE 'truncate table TMP_LI_CFID1';
    EXECUTE IMMEDIATE 'truncate table TMP_LI_CFID2';

    COMMIT;

    /******************************************************************************
    03. INSERT INTO CF ID SUMM
    *******************************************************************************/
    DELETE FROM IFRS_LI_CFID_JOURNAL_INTM_SUMM WHERE DOWNLOAD_DATE=V_CURRDATE;


    INSERT INTO TMP_LI_CFID1(MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT)
    SELECT MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT
    FROM IFRS_LI_CFID_JOURNAL_INTM_SUMM
    WHERE DOWNLOAD_DATE=V_PREVDATE;

    COMMIT;


    INSERT INTO TMP_LI_CFID1(MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT)
    SELECT MASTERID,CF_ID
    ,SUM(CASE WHEN JOURNALCODE2 IN('ITRCG','ITRCG1','ITRCG_SL','ITRCG1_SL') THEN CASE WHEN REVERSE='N' THEN N_AMOUNT ELSE -1 * N_AMOUNT END ELSE 0 END)
    ,SUM(CASE WHEN JOURNALCODE2 IN ('ACCRU','ACCRU_SL') THEN CASE WHEN REVERSE='N' THEN N_AMOUNT ELSE -1 * N_AMOUNT END ELSE 0 END)
    FROM IFRS_LI_ACCT_JOURNAL_INTM
    WHERE DOWNLOAD_DATE=V_CURRDATE
    GROUP BY MASTERID,CF_ID;

    COMMIT;


    INSERT INTO TMP_LI_CFID2(MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT)
    SELECT MASTERID,CF_ID,SUM(ITRCG_AMT),SUM(AMORT_AMT)
    FROM TMP_LI_CFID1
    GROUP BY MASTERID,CF_ID;

    COMMIT;


    INSERT INTO IFRS_LI_CFID_JOURNAL_INTM_SUMM(DOWNLOAD_DATE,MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT,UNAMORT_AMT,CREATEDDATE)
    SELECT V_CURRDATE,MASTERID,CF_ID,ITRCG_AMT,AMORT_AMT,(ITRCG_AMT+AMORT_AMT),SYSTIMESTAMP
    FROM TMP_LI_CFID2;

    COMMIT;

    INSERT INTO IFRS_LI_AMORT_LOG(DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
    VALUES( V_CURRDATE,SYSTIMESTAMP,'END','SP_IFRS_LI_CFID_JRNL_INTM_SUMM','');

    COMMIT;

END;