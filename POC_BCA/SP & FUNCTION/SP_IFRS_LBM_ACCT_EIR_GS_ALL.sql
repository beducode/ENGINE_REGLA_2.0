              WHEN A.INTCALCCODE IN ('1','6') THEN A.I_DAYS / 360 * A.EIR1 / 100 * (A.N_OSPRN_PREV + A.PREV_UNAMORT1)
              --WHEN INTCALCCODE = '3' REMARKS FOR ALIGN WITH ICC PAYMENT SCHEDULE 20160428
              WHEN A.INTCALCCODE IN ('2','3') THEN A.I_DAYS / 365 * A.EIR1 / 100 * (A.N_OSPRN_PREV + A.PREV_UNAMORT1)
              ELSE (A.M / 1200 * A.EIR1 * (A.N_OSPRN_PREV + A.PREV_UNAMORT1))
              /*  BCA DISABLE BPI END */
              END
	,A.EIRAMT2 =
              /*  BCA DISABLE BPI
