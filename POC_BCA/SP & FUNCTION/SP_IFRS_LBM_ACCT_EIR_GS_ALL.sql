                    WHEN A.INTCALCCODE IN ('2','3') THEN A.I_DAYS / 365 * A.EIR_NOCF2 / 100 * (A.N_OSPRN_PREV + A.PREV_UNAMORT_NOCF1)
                    ELSE (A.M / 1200 * A.EIR_NOCF2 * (A.N_OSPRN_PREV + A.PREV_UNAMORT_NOCF1))
                    END - A.N_INT_PAYMENT
                    ) + A.DISB_AMOUNT
