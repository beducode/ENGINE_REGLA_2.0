CREATE OR REPLACE PROCEDURE SP_IFRS_LBM_ACCT_EIR_GS_ALL
AS
  V_CURRDATE   DATE;
  V_PREVDATE   DATE;
  V_VLOOP2     NUMBER(19);
  V_VCNT       NUMBER(19);
  V_VCNT2      NUMBER(19);
  V_COUNTER    NUMBER(19);
  V_MAXCOUNTER NUMBER(19);


BEGIN

  SELECT MAX (CURRDATE),MAX (PREVDATE)
  INTO V_CURRDATE, V_PREVDATE
  FROM IFRS_PRC_DATE_AMORT  ;

  INSERT INTO IFRS_AMORT_LOG (DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
  VALUES(V_CURRDATE, SYSTIMESTAMP, 'START', 'SP_IFRS_LBM_ACCT_EIR_GS_PROCESS', '');

  COMMIT;


	UPDATE /*+ PARALLEL(8) */ IFRS_LBM_ACCT_EIR_PAYM_GS_DATE
	SET EIR = NULL
		,NEXT_EIR = NULL
		,FINAL_EIR = NULL
		,EIR_NOCF = NULL
		,NEXT_EIR_NOCF = NULL
		,FINAL_EIR_NOCF = NULL;

  COMMIT;

	EXECUTE IMMEDIATE 'TRUNCATE TABLE TMP_GS2';

  COMMIT;

	INSERT /*+ PARALLEL(8) */ INTO TMP_GS2 (
		MASTERID
		,DTMIN
		,CNTMIN
		,--ADDING FOR MIN COUNTER
		BENEFIT
		,STAFFLOAN
		,COST_AMT
		,FEE_AMT
		,GAIN_LOSS_FEE_AMT
		,GAIN_LOSS_COST_AMT
		)
	SELECT /*+ PARALLEL(8) */ B.MASTERID
		,C.DTMIN
		,C.CNTMIN
		,--ADDING FOR MIN COUNTER
		B.BENEFIT
		,B.STAFFLOAN
		,B.COST_AMT
		,B.FEE_AMT
		,COALESCE(B.GAIN_LOSS_FEE_AMT, 0) --20180226 GAIN LOSS ADJ
		,COALESCE(B.GAIN_LOSS_COST_AMT, 0)
	FROM IFRS_LBM_ACCT_EIR_CF_ECF1 B
	JOIN IFRS_LBM_ACCT_EIR_PAYM_GS_DATE C ON C.MASTERID = B.MASTERID;

  COMMIT;

  MERGE INTO IFRS_LBM_ACCT_EIR_PAYM_GS A
  USING TMP_GS2 B
  ON (A.MASTERID=B.MASTERID
      AND A.COUNTER=B.CNTMIN)
  WHEN MATCHED THEN
  UPDATE
  SET A.PREV_UNAMORT1 = CASE WHEN B.STAFFLOAN = 1 AND B.BENEFIT < 0  THEN B.BENEFIT
                             WHEN B.STAFFLOAN = 1 AND B.BENEFIT >= 0 THEN 0
                             ELSE B.FEE_AMT - B.GAIN_LOSS_FEE_AMT --20180417
                           END + CASE WHEN B.STAFFLOAN = 1 AND B.BENEFIT <= 0 THEN 0
                                      WHEN B.STAFFLOAN = 1 AND B.BENEFIT > 0  THEN B.BENEFIT
                                      ELSE B.COST_AMT - B.GAIN_LOSS_COST_AMT --20180417
                                      END
		, A.PREV_UNAMORT_NOCF1 = 0
		,--FOR NO CF CALCULATION
		  A.EIR1 = CASE WHEN B.STAFFLOAN = 1 THEN 10.5
                  WHEN N_INT_RATE > 1  THEN N_INT_RATE
                  ELSE 1
                  END
		, A.EIR2 = CASE WHEN B.STAFFLOAN = 1 THEN 11
                    WHEN N_INT_RATE > 1 THEN N_INT_RATE
                    ELSE 1
                    END + (0.01 * CASE WHEN N_INT_RATE > 1 THEN N_INT_RATE ELSE 1 END)
		, A.EIR_NOCF1 = CASE WHEN B.STAFFLOAN = 1 THEN 10.5
                        WHEN N_INT_RATE > 1 THEN N_INT_RATE
                        ELSE 1
                        END
		, A.EIR_NOCF2 = CASE WHEN B.STAFFLOAN = 1 THEN 11
                         WHEN N_INT_RATE > 1 THEN N_INT_RATE
                         ELSE 1
                         END + (0.01 * CASE WHEN N_INT_RATE > 1 THEN N_INT_RATE ELSE 1 END);

  COMMIT;

  INSERT INTO IFRS_AMORT_LOG (DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
	VALUES (V_CURRDATE,SYSTIMESTAMP,'DEBUG','SP_IFRS_LBM_ACCT_EIR_GS_PROCESS','1');


	-- 20131106 DANIEL S : NOTE UNAMORT AMOUNT FOR EACH MASTERID
  MERGE INTO IFRS_LBM_ACCT_EIR_PAYM_GS_DATE A
  USING IFRS_LBM_ACCT_EIR_PAYM_GS B
  ON (A.MASTERID=B.MASTERID
      AND A.DTMIN=B.PMT_DATE)
  WHEN MATCHED THEN
  UPDATE
  SET A.UNAMORT=B.PREV_UNAMORT1
    , A.UNAMORT_NOCF=B.PREV_UNAMORT_NOCF1;

  COMMIT;

	INSERT INTO IFRS_AMORT_LOG (DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
