CREATE OR REPLACE PROCEDURE USPS_LOAD_SEGMENT
(
  v_DOWNLOAD_DATE IN DATE DEFAULT NULL ,
  V_DATA_SOURCE IN VARCHAR2 DEFAULT NULL,
  Cur_out OUT SYS_REFCURSOR
)
AS
BEGIN



      OPEN  Cur_out FOR

SELECT DISTINCT SEGMENT, SEGMENT AS DESCRIPTION  FROM IFRS_MASTER_ACCOUNT
 WHERE DOWNLOAD_DATE = v_DOWNLOAD_DATE and data_source = V_DATA_SOURCE;


END;