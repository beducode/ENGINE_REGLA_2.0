CREATE OR REPLACE PROCEDURE USPS_PENDINGJOBS
(
  Cur_out OUT SYS_REFCURSOR
)
AS
BEGIN

    OPEN  Cur_out FOR
    SELECT * FROM TBLT_SERVICEJOBSHEADER A
    --LEFT JOIN  TBLT_APPLICATIONLOG B
    --ON  A.PKID = B.JOBID
    WHERE
    --B.JOBID IS NULL AND
    A.STATUS IN (SELECT DESCRIPTION FROM TBLM_COMMONCODEDETAIL WHERE COMMONCODE = 'B129' AND VALUE1 = 1)
        AND A.JOBNAME <> 'RUN_PD_INTERPOLATE'
    ORDER by PKID;

END;