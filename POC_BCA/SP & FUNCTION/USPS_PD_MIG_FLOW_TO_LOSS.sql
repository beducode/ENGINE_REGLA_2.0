CREATE OR REPLACE PROCEDURE USPS_PD_MIG_FLOW_TO_LOSS (
    v_eff_date    date,
    v_pd_rule_id  number,
    Cur_out OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN Cur_out FOR
    SELECT D.PD_RULE_NAME "PD Model", A.BASE_DATE "Period From",
    A.EFF_DATE    "Period To", A.BUCKET_ID "Bucket ID",
    B.BUCKET_NAME "Bucket Name", ROUND((A.FLOW_TO_LOSS * 100), 6) "Roll Rate (%)"
    FROM IFRS_PD_MIG_FLOW_TO_LOSS A
    JOIN IFRS_BUCKET_DETAIL B
    ON A.BUCKET_GROUP = B.BUCKET_GROUP
    AND A.BUCKET_ID = B.BUCKET_ID
    JOIN IFRS_PD_RULES_CONFIG D
    ON A.PD_RULE_ID = D.PKID
    AND A.PD_RULE_ID = TO_CHAR(v_pd_rule_id)
    AND A.EFF_DATE BETWEEN ADD_MONTHS(TO_CHAR(v_eff_date,'dd MON yyyy'), -1 * D.HISTORICAL_DATA )  AND  TO_CHAR(v_eff_date,'dd MON yyyy')
    ORDER BY A.BASE_DATE DESC, B.BUCKET_ID;
END;