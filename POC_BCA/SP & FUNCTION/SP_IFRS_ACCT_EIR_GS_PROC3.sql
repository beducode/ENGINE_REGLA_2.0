	MERGE INTO IFRS_ACCT_EIR_PAYM_GS_DATE A
	USING IFRS_ACCT_EIR_PAYM_GS B
	ON (B.MASTERID = A.MASTERID AND B.PMT_DATE = A.DTMIN)
	WHEN MATCHED THEN
	UPDATE SET A.UNAMORT = B.PREV_UNAMORT1;

	COMMIT;

	INSERT INTO IFRS_AMORT_LOG (
		DOWNLOAD_DATE
		,DTM
		,OPS
		,PROCNAME
		,REMARK
		)
