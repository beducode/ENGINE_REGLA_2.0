CREATE OR REPLACE PROCEDURE SP_IFRS_LI_CHECK_AMORT
AS
  V_CURRDATE DATE;
  V_PREVDATE DATE  ;
  V_VCNT NUMBER(10);
BEGIN

    /******************************************************************************
    01. DECLARE VARIABLE
    *******************************************************************************/
    SELECT MAX(CURRDATE), MAX(PREVDATE)
    INTO V_CURRDATE, V_PREVDATE
    FROM IFRS_LI_PRC_DATE_AMORT;

    INSERT INTO IFRS_LI_AMORT_LOG(DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
    VALUES(V_CURRDATE,SYSTIMESTAMP,'START','SP_IFRS_LI_CHECK_AMORT','');

    EXECUTE IMMEDIATE 'truncate table IFRS_LI_CHECK_AMORT'  ;
    EXECUTE IMMEDIATE 'truncate table TMP_LI_X'  ;

    COMMIT;

    /******************************************************************************
    02. INSERT INTO TEMP
    *******************************************************************************/
    INSERT INTO TMP_LI_X(DOWNLOAD_DATE,CCY,MASTERID,ACCTNO,JOURNALCODE,AMOUNT)
    SELECT DOWNLOAD_DATE,CCY,MASTERID, ACCTNO
         , CASE WHEN JOURNALCODE IN('ACCRU','ACCRU_SL')THEN 'AMORT' WHEN JOURNALCODE IN ('DEFA0','DEFA1') THEN 'DEFA0' ELSE  JOURNALCODE END AS JOURNALCODE
         , SUM(CASE WHEN REVERSE='Y' THEN -1 * N_AMOUNT ELSE N_AMOUNT END) AS AMOUNT
    FROM IFRS_LI_ACCT_JOURNAL_INTM_SUMM
    WHERE DOWNLOAD_DATE=V_CURRDATE AND  JOURNALCODE NOT IN ('ACRU4','AMRT4')
    GROUP BY DOWNLOAD_DATE,CCY
    , CASE WHEN JOURNALCODE IN('ACCRU', 'ACCRU_SL')  THEN 'AMORT' WHEN JOURNALCODE IN ('DEFA0','DEFA1') THEN 'DEFA0' ELSE  JOURNALCODE END,MASTERID, ACCTNO;

    COMMIT;


    EXECUTE IMMEDIATE 'truncate table TMP_LI_X1'  ;
    INSERT INTO TMP_LI_X1(DOWNLOAD_DATE,CCY,MASTERID,ACCTNO,JOURNALCODE,AMOUNT)
    SELECT DOWNLOAD_DATE,CCY,MASTERID,ACCTNO,JOURNALCODE,AMOUNT FROM TMP_LI_X WHERE JOURNALCODE='DEFA0';

    COMMIT;

    EXECUTE IMMEDIATE 'truncate table TMP_LI_X2'  ;
    INSERT INTO TMP_LI_X2(DOWNLOAD_DATE,CCY,MASTERID,ACCTNO,JOURNALCODE,AMOUNT)
    SELECT DOWNLOAD_DATE,CCY,MASTERID,ACCTNO,JOURNALCODE,AMOUNT FROM TMP_LI_X WHERE JOURNALCODE!='DEFA0';

    COMMIT;

    /******************************************************************************
    03. INSERT CHECK AMORT
    *******************************************************************************/
    INSERT INTO IFRS_LI_CHECK_AMORT
    (
    DOWNLOAD_DATE,MASTERID,CCY,DEFA0_AMT,AMORT_AMT,UNAMORT_AMT,ACCOUNT_NUMBER
    )
    SELECT
    NVL(A.DOWNLOAD_DATE,B.DOWNLOAD_DATE) AS DOWNLOAD_DATE2
    , NVL(A.MASTERID,B.MASTERID) AS MASTERID2
    , NVL(A.CCY,B.CCY) AS CCY
    , CASE WHEN A.JOURNALCODE = 'DEFA0' THEN A.AMOUNT ELSE B.AMOUNT END AS DEFA0_AMT
    , B.AMOUNT AS AMORT_AMT
    , NVL(A.AMOUNT,0) + NVL(B.AMOUNT,0) AS UNAMORT_AMT
    , A.ACCTNO
    FROM TMP_LI_X1 A
    FULL OUTER JOIN TMP_LI_X2 B ON B.MASTERID=A.MASTERID AND A.CCY=B.CCY AND A.ACCTNO = B.ACCTNO;

    COMMIT;

    /******************************************************************************
    04. UPDATE CHECK AMORT
    *******************************************************************************/
    MERGE INTO IFRS_LI_CHECK_AMORT A
    USING IFRS_LI_MASTER_ACCOUNT B
    ON (B.MASTERID=A.MASTERID
              AND  B.ACCOUNT_NUMBER = A.ACCOUNT_NUMBER
            AND B.DOWNLOAD_DATE=V_CURRDATE
       )
    WHEN MATCHED THEN
    UPDATE
    SET  A.IMA_UNAMORT_AMT = NVL(B.UNAMORT_FEE_AMT,0)+NVL(B.UNAMORT_COST_AMT,0)
        ,A.IMA_OUTSTANDING = B.OUTSTANDING
        ,A.CONTROL_AMT = NVL(A.DEFA0_AMT,0) + NVL(A.AMORT_AMT,0) - (NVL(B.UNAMORT_FEE_AMT,0)+NVL(B.UNAMORT_COST_AMT,0));
    COMMIT;


    INSERT INTO IFRS_LI_AMORT_LOG(DOWNLOAD_DATE,DTM,OPS,PROCNAME,REMARK)
    VALUES(V_CURRDATE,SYSTIMESTAMP,'END','SP_IFRS_LI_CHECK_AMORT','');
    --/*remark by adam
    COMMIT;

    /******************************************************************************
    05. IF ERROR
    *******************************************************************************/
    SELECT COUNT (*)
    INTO V_VCNT
    FROM IFRS_LI_CHECK_AMORT
    WHERE ABS (CONTROL_AMT) > 1;


    IF V_VCNT > 0
    THEN  RAISE_APPLICATION_ERROR (-20002,'CHECK AMORT ERROR!');
       --   RETURN;
    END IF;

    COMMIT;

END;