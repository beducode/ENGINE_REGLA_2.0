CREATE OR REPLACE PROCEDURE USPS_PARTIAL_RUN_HEADER
(
    V_PERIOD DATE DEFAULT '1900-01-01',
    Cur_out OUT SYS_REFCURSOR
)
AS
BEGIN

    OPEN Cur_out FOR
        SELECT A.PKID, A.DOWNLOAD_DATE, A.RUN_SEQUENCE, A.REASON, A.RUN_STATUS, B.DESCRIPTION AS STATUS FROM IFRS_PARTIAL_RUN_HEADER A JOIN TBLM_COMMONCODEDETAIL B
        ON TO_CHAR(A.RUN_STATUS) = B.VALUE1 AND B.COMMONCODE = 'B129'
        WHERE A.DOWNLOAD_DATE = V_PERIOD;

END;