CREATE OR REPLACE PROCEDURE  USPS_LOOKUPSEARCHDATA
(
    v_grid_name IN VARCHAR2 DEFAULT ' ',
    Cur_out OUT SYS_REFCURSOR
)
AS
BEGIN

    OPEN Cur_out FOR
        SELECT FIELDNAME, COLUMNTYPE
        FROM TBLS_GRIDCOLUMNS
        WHERE SHOWSELECT = '1' AND
            COLUMNTYPE <> 'HYPERLINK' AND
            COLUMNTYPE <> 'CHECK' AND
            GRIDID = v_grid_name;

END;