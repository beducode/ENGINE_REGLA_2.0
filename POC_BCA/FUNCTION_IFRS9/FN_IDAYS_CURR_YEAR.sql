CREATE OR REPLACE FUNCTION FN_IDAYS_CURR_YEAR (DATE1 IN DATE) RETURN NUMBER AS
    V1 NUMBER;
    YEAR1 DATE;
    YEAR2 DATE;
BEGIN
    YEAR1:=TO_DATE('31-12-'|| EXTRACT (YEAR FROM DATE1),'DD-MM-YYYY');
    YEAR2:=TO_DATE('31-12-'|| (EXTRACT (YEAR FROM DATE1)-1),'DD-MM-YYYY');

    V1:=YEAR1-YEAR2;

RETURN V1;
END;