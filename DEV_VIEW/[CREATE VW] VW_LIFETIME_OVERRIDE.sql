DROP VIEW IF EXISTS VW_LIFETIME_OVERRIDE;
CREATE VIEW VW_LIFETIME_OVERRIDE
AS
SELECT DISTINCT B.PKID, B.LIFETIME_RULE_NAME AS "lifetime_configuration",
B.SEGMENTATION_ID AS "segmentation_id", 
A.SEGMENT AS "segmentation", 
A.DOWNLOAD_DATE, 
0 AS "lifetime_rate", 
B.LIFETIME_OVERRIDE AS "lifetime_override"
FROM IFRS_MASTER_ACCOUNT A
INNER JOIN IFRS_LIFETIME_RULES_CONFIG B ON A.SEGMENTATION_ID = B.SEGMENTATION_ID
WHERE A.DOWNLOAD_DATE = (SELECT CURRDATE FROM IFRS_PRC_DATE)
AND B.IS_DELETE = 0 AND B.ACTIVE_FLAG = 1;