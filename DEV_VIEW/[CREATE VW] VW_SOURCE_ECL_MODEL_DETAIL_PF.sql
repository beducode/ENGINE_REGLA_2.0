AS
 SELECT PKID,
    ECL_MODEL_ID,
    SEGMENTATION_ID,
    LT_RULE_ID,
    SICR_RULE_ID,
    UPSIDE_VALUE,
    BASE_VALUE,
    DOWNSIDE_VALUE,
