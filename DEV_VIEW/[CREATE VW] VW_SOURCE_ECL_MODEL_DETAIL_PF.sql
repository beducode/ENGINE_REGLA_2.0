    BASE_PROB_OUTCOME,
    DOWNSIDE_PROB_OUTCOME,
    OVERLAY_AMOUNT,
    IS_DELETE,
    CREATEDBY,
    CREATEDDATE,
    CREATEDHOST,
    UPDATEDBY,
    UPDATEDDATE,
    UPDATEDHOST,
    BUCKET_GROUP,
    DEFAULT_RULE_ID
   FROM ( SELECT PKID,
            ECL_MODEL_ID,
            SEGMENTATION_ID,
            LT_RULE_ID,
            SICR_RULE_ID,
            UPSIDE_VALUE,
            BASE_VALUE,
            DOWNSIDE_VALUE,
            UPSIDE_PROB_OUTCOME,
            BASE_PROB_OUTCOME,
            DOWNSIDE_PROB_OUTCOME,
            OVERLAY_AMOUNT,
            IS_DELETE,
            CREATEDBY,
            CREATEDDATE,
            CREATEDHOST,
            UPDATEDBY,
            UPDATEDDATE,
            UPDATEDHOST,
            BUCKET_GROUP,
            DEFAULT_RULE_ID
           FROM VW_IFRS_ECL_MODEL_DETAIL_PF
        EXCEPT
         SELECT PKID,
            ECL_MODEL_ID,
            SEGMENTATION_ID,
            LT_RULE_ID,
            SICR_RULE_ID,
            UPSIDE_VALUE,
            BASE_VALUE,
            DOWNSIDE_VALUE,
            UPSIDE_PROB_OUTCOME,
            BASE_PROB_OUTCOME,
            DOWNSIDE_PROB_OUTCOME,
            OVERLAY_AMOUNT,
            IS_DELETE,
            CREATEDBY,
            CREATEDDATE,
            CREATEDHOST,
            UPDATEDBY,
            UPDATEDDATE,
            UPDATEDHOST,
            BUCKET_GROUP,
            DEFAULT_RULE_ID
           FROM IFRS_ECL_MODEL_DETAIL_PF) CHECK_IFRS_ECL_MODEL_DETAIL_PF;