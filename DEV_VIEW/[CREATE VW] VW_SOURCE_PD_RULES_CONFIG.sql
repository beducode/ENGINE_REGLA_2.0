	SELECT PKID
	,TM_RULE_NAME
	,SEGMENTATION_ID
	,PD_METHOD
	,CALC_METHOD
	,BUCKET_GROUP
	,EXPECTED_LIFE
	,INCREMENT_PERIOD
	,HISTORICAL_DATA
	,CUT_OFF_DATE
	,ACTIVE_FLAG
	,IS_DELETE
	,CREATEDBY
	,CREATEDDATE
	,CREATEDHOST
	,LAG_1MONTH_FLAG
	,RUNNING_STATUS FROM VW_PD_RULES_CONFIG
	EXCEPT
	SELECT PKID
	,TM_RULE_NAME
	,SEGMENTATION_ID
	,PD_METHOD
	,CALC_METHOD
	,BUCKET_GROUP
	,EXPECTED_LIFE
	,INCREMENT_PERIOD
	,HISTORICAL_DATA
	,CUT_OFF_DATE
	,ACTIVE_FLAG
	,IS_DELETE
	,CREATEDBY
	,CREATEDDATE
	,CREATEDHOST
	,LAG_1MONTH_FLAG
	,RUNNING_STATUS FROM IFRS_PD_RULES_CONFIG
) CHECK_PD_RULES_CONFIG;