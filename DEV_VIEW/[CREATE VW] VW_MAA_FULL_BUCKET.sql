DROP VIEW IF EXISTS VW_MAA_FULL_BUCKET;

CREATE VIEW VW_MAA_FULL_BUCKET AS
SELECT DISTINCT A.BUCKET_GROUP,A.BUCKET_ID AS BUCKET_FROM,B.BUCKET_ID AS BUCKET_TO 
FROM IFRS_BUCKET_DETAIL A
CROSS JOIN  IFRS_BUCKET_DETAIL B 
WHERE A.BUCKET_GROUP = B.BUCKET_GROUP AND A.ACTIVE_FLAG = 1 AND A.IS_DELETE = 0 AND B.ACTIVE_FLAG = 1 AND B.IS_DELETE = 0;